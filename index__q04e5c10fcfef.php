==Overview==
Missiles are expendable munitions.

Most attack missiles and torpedoes detonate to cause damage to shields and hulls.  Their primary purpose, to distinguish themselves from energy [[weapons]], is that they consume much more space and functionally cost you money to attack, but compensate by being capable of inflicting far more damage in a given period of time than a regular weapon can.  A good many types of missiles cannot be bought at all, and must be hoarded from enemy kills, making them more precious as a weapon, or else simply sellable loot from a kill.  Many powerful missiles and torpedoes have ranges far greater than normal lasers, making them capable of being used from "safe" ranges where a smaller ship would be vaporized by capital ship fire if they traveled into standard weapons range.  

This means missiles have a large role in [[Combat#Combat_strategy|combat strategy]], as it gives weaker ships a chance to kill stronger ships, if they are willing to sacrifice money or rare loot, or simply have the [[Stations|missile complexes]] to buy victory.

[[Boarding pods]] are a "missile" which delivers marines from the firing ship to the target.

Attack missiles have a variety of properties.&lt;bR>
'''Damage yield''' is the amount of damage inflicted on targets within the '''Area of Effect'''.&lt;br>
'''Range''' describes how far the missile can travel, generally you want to launch at targets well within range to compensate for target movement.&lt;br>
'''Speed''' describes how quickly it moves towards a target.&lt;br>
'''Cooldown''' describes how much time the launcher takes between firings to prepare the next launch.&lt;br>
'''Seeker''' determines how the missile acquires targets and when it may be fired: '''Targetted''', requiring a target prior to launch or '''Autonomous''' which relies on the missile's onboard systems.&lt;br>
'''Swarming''' refers simply to missiles which release multiple warheads. There are always eight '''warheads''' released. Note: warhead behavior is detailed in other columns such as dumbfire, heat-seeking, etc.&lt;br>
'''Guidance''' determines how a missile tracks and follows targets: '''Heat seeking''' missiles will attempt to attack the nearest warm target, '''Image Recognition''' or '''Standard Guidance''' will follow a specific target primarily based on its appearance, '''Dumb-fire''' have no guidance at all and will travel in direction of launch without maneuvering relying on the pilot's intuition of target movement.&lt;br>
'''Fusing''' controls detonation, most missiles detonate when they '''impact''' a target. '''Proximity fusing''' will detonate when the missiles gets close enough to the target, generally once it's already missed impact and is getting farther away. A few missiles may be '''Manually Triggered''' to detonate remotely, also known as '''synaptic triggering'''.&lt;br>
Some missiles have extremely powerful detonations which affect large areas the size of this influence is noted by '''Area of Effect'''.&lt;br>

