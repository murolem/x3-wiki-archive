<div id="mw-content-text" lang="en" dir="ltr" class="mw-content-ltr"><p>There are a lot of different ranks in X3. There are <a href="/index.php/Reputation" title="Reputation">race ranks</a>, <a href="/index.php/Combat" title="Combat">combat</a> ranks and trade ranks.
Below you can get a quick overview of combat and trade ranks.
</p><p>Rank is a representation of character reputation in the <a href="/index.php/X-universe" title="X-universe">X-universe</a>. Killing ships increases Combat Rank, reflecting the character's reputation of combat skill. Conducting trades increases Trade Rank, reflecting the character's reputation as an expert trader.
</p><p><a href="/index.php/Missions" title="Missions">Missions</a> often come with certain rank requirements, simulating a willingness by NPCs to trust the character with their vital interests.
</p><p><a href="/index.php/Ships" title="Ships">Ships</a> and <a href="/index.php/Wares" title="Wares">wares</a> usually have race rank requirements, reflecting a need for the selling faction to have a certain amount of trust in the character.
</p><p>Combat ranks are also used by the game to set up opposition forces. A higher combat rank generally implies that you have some skill at fighting.  As a consequence the game likes to increase the challenge of fights by spawning heavier and more numerous combatants for you to face.
</p><p>Per the Offical Egosoft FAQ:
If you leave the game idle for a long period of time then your fight and trade ranks will start to decay.  Your fight rank will drop by about 2% for every game hour without player input (or for every 6 minutes, on 1000% SETA), and your trade rank by around 0.25%. 
</p><p>It has been tested that the lowest your Combat and Trade ranks will fall is Fighter and Master Dealer, respectively.
</p>
