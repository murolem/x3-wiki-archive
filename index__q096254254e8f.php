<h2><span class="mw-headline" id="Description">Description</span></h2>
<p>Of little use to the Paranid, this system is leased by the Boron who have a budding outpost of refineries and chemical plants here. Limited trade between the <a href="/index.php/Paranid" title="Paranid">Paranid</a> and <a href="/index.php/Boron" title="Boron">Boron</a> takes place here and relations between the species are amicable. In return for Paranid protection the Boron lend their extensive knowledge in biochemistry to the Paranid efforts in cyber biology.
</p>
