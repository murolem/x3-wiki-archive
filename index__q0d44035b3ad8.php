You may want to select the singular version of the topic you're writing about, this will simplify linking it in the future.
so naming a page dock instead of docking will permit linking by &lt;nowiki>[[dock]]ing&lt;/nowiki> similarly for ship vs. ships. general wikipedia style suggests the opposite, but that may be subject to change. doing it the other way requires use of aliasing to create the right link with the desired text.

===Some basic commands===
links put your text between &lt;nowiki>[[text]] &lt;/nowiki>to link it to the page with the same name. Afterwords you can create that page by clicking the link.&lt;br>
You can use different text than the linked page by adding a bar like:&lt;nowiki>[[text|different text]]&lt;/nowiki> which would link to text but display different text.

Best thing for stuff like ships etc is to put it in a sortable table. This way you can sort it on race price or anything else. With this it is also possible to sort (for example) argon m4 ships.


Create new table example
Please not that every column of a row starts with a | and the title of a column with !! at the top. You only need to add the names ones and can then keep adding rows.
:

#{| class="prettytable sortable" border="1" cellpadding="5" cellspacing="0"
#|+ All Ships
# ! Name !! Race !! class !! Max shields !! Num shields !! Shield type !! Turrets !! Weapon generator !! Shield generator !! Max #speed !! Max cargo !! Cargo type !! Hangar !! Price   

#|- style="background:white; color:black"
#| [[Mammoth]]
