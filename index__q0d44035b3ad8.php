===Templates===
To quote the wikipedia help article: "A template is a page created explicitly for transclusion â€“ the process of including the contents of one page within another page."

templates are handy for standardizing a regularly reused display- of information.

a complete list of pages can be found [[Special:PrefixIndex]]
and a complete list of templates can be found by selecting "Templates"

templates are generally invoked like: &lt;nowiki>{{M1class}}&lt;/nowiki>&lt;bR>
Which will generate output like: {{M1class}}

When writing new templates you can control what gets output in a call with
&lt;nowiki>&lt;includeonly> and &lt;noinclude>&lt;/nowiki> tags
includeonly will cause the contained content to only appear when the template itself is called.
noinclude will only show the contained content when the template is viewed directly.

This is useful when making documentation for your templates. it is also useful when you're trying to do things with categories.

'''try to avoid extra line spacing in includes'''. the spaces improve readability, but remember that they also appear where included, in infoboxes this will often leave unsightly gaps above the table, 

more on this subject on the wikipedia help articles: {{linky}}en.wikipedia.org/wiki/Help:Templates

advanced templates:&lt;br>
{{linky}}meta.wikimedia.org/wiki/Help:Advanced_templates&lt;bR>
pay particular attention to the "Branching techniques without ParserFunctions" section if you're interested in if-then type scenarios. tricky though and requires some precision in usage.

===Categories===
categories use some tagging feature built into wikipedia, it's not required to use and will require some consensus when or where to implement them.
{{linky}}en.wikipedia.org/wiki/Wikipedia:Categorization

pages can be categorized by adding &lt;nowiki>[[Category:(category name)]]&lt;/nowiki>&lt;bR>
If there is no existant category page you may create one which will provide textual context for the list of links.&lt;bR>
you may refer to these pages by adding a semicolon before the category tag like: &lt;nowiki>[[:Category:(category name)]]&lt;/nowiki>&lt;bR>
