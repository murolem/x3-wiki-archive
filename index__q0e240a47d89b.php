<h2><span class="mw-headline" id="Overview">Overview</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Gauss_Cannon&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>The Gauss Cannon, sometimes abbreviated to GC, is a <a href="/index.php/Teladi" title="Teladi">Teladi</a> capital ship weapon that uses <a href="/index.php/Gauss_Cannon_Ammunition" title="Gauss Cannon Ammunition">ammunition</a>.
</p>
<h2><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Gauss_Cannon&amp;action=edit&amp;section=2" title="Edit section: Encyclopedia Entry">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>A deadly weapon found on frigates, destroyers and carriers. The Teladi designed this ammo-based low-tech solution in a bid to save money on expensive capital ship energy generators. The Gauss Cannon uses magnetic coils to accelerate large ionised metallic slugs that, while too big to pass through them, will disrupt enemy shields. This weapon requires specially-made ammunition in order to fire.
</p>
<h2><span class="mw-headline" id="Commentary">Commentary</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Gauss_Cannon&amp;action=edit&amp;section=3" title="Edit section: Commentary">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>A powerful and popular ammo-based capital weapon. The Gauss Cannon is very ammunition efficient and effective against both shields and hull. Its damage leans towards rapid hull destruction rather than shielding, but it deals good shield damage also. It is often used in conjunction with the <a href="/index.php/Ion_Cannon" title="Ion Cannon">Ion Cannon</a> which has similar range and the highest shield damage in the game.
</p>
