<h2><span class="mw-headline" id="Suggested_Roles">Suggested Roles</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Solano&amp;action=edit&amp;section=9" title="Edit section: Suggested Roles">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>The Solano is a player favorite for use as a cheap carrier fighter - good survivability, speed and firepower, along with <a href="/index.php/Mass_Driver" title="Mass Driver">Mass Driver</a> compatibility, make it extremely dangerous to face in large numbers.
</p><p>The ship is poor against Corvette and Capital ships, however if used correctly, it can dodge most of the incoming fire from Anti-fighter cannons, with the exceptions of flak artillery and Kyon weaponry. The ship should therefore primarily be deployed against enemy fighters and bombers, while other craft target heavier vessels.
</p>
