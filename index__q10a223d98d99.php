<h2><span class="mw-headline" id="Overview">Overview</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Solano&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>The Solano is an extremely deadly ship for its size. Boasting all the speed and manoeuvrability of an average interceptor, its shields outclass some heavy fighters. Do not underestimate this <a href="/index.php/OTAS" title="OTAS">OTAS</a> fighter.
</p>
<h2><span class="mw-headline" id="Background">Background</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Solano&amp;action=edit&amp;section=2" title="Edit section: Background">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<h3><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Solano&amp;action=edit&amp;section=3" title="Edit section: Encyclopedia Entry">edit</a><span class="mw-editsection-bracket">]</span></span></h3>
<p>The Solano is an advanced interceptor, optimised for rapid closure on hostile intruders. It has the ability to destroy similar-sized ships or hold larger ones until reinforcements arrive.
</p>
<h2><span class="mw-headline" id="Physical_Characteristics">Physical Characteristics</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Solano&amp;action=edit&amp;section=4" title="Edit section: Physical Characteristics">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<h2><span class="mw-headline" id="Combat">Combat</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Solano&amp;action=edit&amp;section=5" title="Edit section: Combat">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>Due to its heavy shielding, the Solano is even more difficult to kill than it is to catch. With its ability to mount both <a href="/index.php/PAC" title="PAC" class="mw-redirect">PAC</a> and <a href="/index.php/PRG" title="PRG" class="mw-redirect">PRG</a>, the latter an exceptional dogfighting weapon and the former a great all-rounder, it can be tuned to a variety of tasks.
</p><p>It resembles a more heavily shielded, scaled down <a href="/index.php/Mamba" title="Mamba">Mamba</a> with lighter weapons and weaker weapons recharge.
</p>
