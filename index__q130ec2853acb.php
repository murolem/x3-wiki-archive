<h2><span class="mw-headline" id="Overview">Overview</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Energy_Cells&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>With the unification of the Energy Standard, trading this valuable commodity became possible.
</p>
<h2><span class="mw-headline" id="Commentary">Commentary</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Energy_Cells&amp;action=edit&amp;section=2" title="Edit section: Commentary">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>Energy Cells are the most ubiquitous trading ware in the <a href="/index.php/X-universe" title="X-universe" class="mw-redirect">X-universe</a>. Every factory requires energy cells to run and thus can be sold universally across factions.  
</p><p>While low priced, the sheer volume of trade makes energy cell trade and transport very profitable. There are many regions of space where solar plants are common or readily available, leaving good money making opportunities.
</p><p>It is not however, the most profitable trading good, nor does it have the best return on investment.
</p><p>Use of a <a href="/index.php/Jumpdrive" title="Jumpdrive">Jumpdrive</a> uses up a determined amount of energy cells.
</p>
