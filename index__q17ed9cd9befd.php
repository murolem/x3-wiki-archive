<h2><span class="mw-headline" id="Overview">Overview</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Shield_Production_Facility_200MJ&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>200 MJ Shield Production Facilities produce the <a href="/index.php/Shields#200MJ" title="Shields">200 MJ Shield</a> from <a href="/index.php/Energy_Cells" title="Energy Cells">Energy Cells</a>, <a href="/index.php/Ore" title="Ore">Ore</a> and <a href="/index.php/Food" title="Food" class="mw-redirect">Food</a>. 
</p>
<h3><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Shield_Production_Facility_200MJ&amp;action=edit&amp;section=2" title="Edit section: Encyclopedia Entry">edit</a><span class="mw-editsection-bracket">]</span></span></h3>
<p>This production facility manufactures the medium defense shields used by heavy fighters and corvettes. Shields are vital for survival in space. They protect the ship from small impacting objects like mini asteroids as well as during fights.
</p>
