<h2><span class="mw-headline" id="Overview">Overview</span></h2>
<p>A transporter device permits transport of <a href="/index.php/Wares" title="Wares">wares</a> and personnel instantaneously between locations.
</p><p>This typically occurs between ships, but may also include pilots returning to ships or <a href="/index.php/Marines" title="Marines">Marines</a> being picked up by their boarding ship.
</p><p>Transport is typically limited to 5 km.
</p>
