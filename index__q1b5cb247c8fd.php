<h3><span class="editsection">[<a href="/index.php?title=Galaxy_Editor&amp;action=edit&amp;section=1" title="Edit section: Activation">edit</a>]</span> <span class="mw-headline" id="Activation">Activation</span></h3>
<p>To activate the GE, rename your pilot to "Makeanapplepiefromscratch". You will find a new "Game Start" when you create a new game called "Galaxy Editor". It is found below "Custom Game". You will be prompted with two options: "Revelation", which is the Game galaxy in X3:AP and "&lt;New Map&gt;". If you modify "Revelation", you will change the map in which you are currently playing with your saves. (be careful)
</p><p>The Galaxy Editor enables you to do pretty much anything when it comes to creation. You can make new sectors, stations, Suns, Nebulas, Gates, Ships and Asteroids.
</p><p><br />
</p>
<h3><span class="editsection">[<a href="/index.php?title=Galaxy_Editor&amp;action=edit&amp;section=2" title="Edit section: New set of Controls">edit</a>]</span> <span class="mw-headline" id="New_set_of_Controls">New set of Controls</span></h3>
<p>The Galaxy Editor adds the "Development" Section in the Controls menu.
</p><p>Some of these controls are intuitive and some are not.
</p><p><br />
</p>
<h3><span class="editsection">[<a href="/index.php?title=Galaxy_Editor&amp;action=edit&amp;section=3" title="Edit section: Usage">edit</a>]</span> <span class="mw-headline" id="Usage">Usage</span></h3>
<p>To use the Galaxy Editor, press &lt;Shift&gt; + &lt;Keypad Enter&gt; (Can be changed in controls) to open up the Creation and modification window.
</p><p>In the Sector Map window, you are given a list of options. These include the ability to set the properties of objects.
</p><p>When you dock in a station, it enables you to add wares and properties.
</p><p>To Play your new map, just use the &lt;Custom Game&gt; option when you start a new game and select your Galaxy.
</p><p><br />
</p>
