<div id="mw-content-text" lang="en" dir="ltr" class="mw-content-ltr"><p>The Galaxy Editor is an Easter Egg (mostly used by Egosoft staff) and enables players to modify the pre-existing galaxy, or to create a new galaxy.
</p><p><br />
</p>
