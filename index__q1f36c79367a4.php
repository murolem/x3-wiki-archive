<h2><span class="mw-headline" id="Description">Description</span></h2>
<h3><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span></h3>
<p>This mining outpost is surrounded by a field of mineral bearing asteroids with a particularly dense asteroid field to the north. Pirate activity is light and generally non-aggressive, mostly transports headed for the north gate. This is fortunate as high-speed combat in this system is made difficult by the asteroids which also play havoc with missile targeting scanners.
</p>
<h3><span class="mw-headline" id="Information">Information</span></h3>
<p>In Albion Prelude, Plutarch has moved their headquarters to operations to the Albion systems, along with, apparently, most of the asteroids in this sector.
</p>
