<div lang="en" dir="ltr" class="mw-content-ltr"><p>A new universe map is being produced by <a href="/index.php?title=User:Whitehorn&amp;action=edit&amp;redlink=1" class="new" title="User:Whitehorn (page does not exist)">Whitehorn</a>, me. This map will be in PDF format and will include such things as faction ownership of sectors, sector contents (stations, asteroids), clear directionality, and will be vector-based art (which means any level of zoom will be absolutely clear and absent of pixelation). The idea behind this project is to have a map that readily displays all sectors and contents so as to better plan for potential trade routes. The map should represent the start game, as the universe will dynamically add and remove stations from sectors. If you want to help me by providing accurate and complete contents of various sectors, contact Whitehorn on this website. Also, it would be great to be able to upload files to this wiki.
</p><p>Below is a link to the latest PDF (2/07/2012). The sectors marked with flames in the lower left corner are viewed as "finished", having all the stations and asteroids listed in the sector.
</p><p><a rel="nofollow" class="external autonumber" href="http://dl.dropbox.com/u/40583874/whitehornTCmapV1.pdf">[1]</a>
