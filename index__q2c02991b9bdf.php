<h2><span class="mw-headline" id="Overview">Overview</span></h2>
<p>Nividium is a very expensive commodity.
</p><p>Currently, there are no Nividium mining stations available for acquisition in the <a href="/index.php/X-universe" title="X-universe" class="mw-redirect">X-universe</a>.  Consequently, Nividium may only be collected through <a href="/index.php/Mobile_Mining" title="Mobile Mining">Mobile Mining</a>.
</p><p>Not very many places buy Nividium, but there is an exploit enabling you to sell it.
</p>
<h2><span class="mw-headline" id="The_Kha.27ak"><a href="/index.php/Kha%27ak" title="Kha'ak">The Kha'ak</a></span></h2>
<p>Though these insects have never given us the chance to confirm this, they have some sort of relation to Nividium. They value this precious material more than their own lives. So much so they have staged a galactic conflict solely over this material and will protect it with everything they have. It is very likely that their structures and space vessels are made primarily with Nividium and their dominated sectors are usually VERY rich in Nividium.
</p>
