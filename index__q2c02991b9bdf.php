<h2><span class="mw-headline" id="Nvidium_for_Profits">Nvidium for Profits</span></h2>
<p>There are exactly three stations in the galaxy that will buy Nividium. With that said they will only buy two units each; it is easy to gather lots of Nividium and not be able to sell it and feel like you are making something of yourself. Below is a detailed method on how to mine LOTS of Nividium and sell it for TONS of credits.
</p><p><b>Warning reading any further and implementing this method will result in more credits than you will know what to do with.</b>
</p><p>First aquire a few hundred thousand credits, this is usually achievable for most people with one plot mission in TC. Also a fairly friendly reputation with the Argon makes this easy to follow
</p><p>Next find <a href="/index.php/Argon_Prime" title="Argon Prime">Argon Prime</a>, buy a <a href="/index.php/Mercury" title="Mercury">Mercury</a>, the cheapest and outfit it with the following:
</p><p><a href="/index.php/Ore_Collector" title="Ore Collector">Ore Collector</a>, <a href="/index.php/Mineral_Scanner" title="Mineral Scanner">Mineral Scanner</a>, <a href="/index.php/Special_Command_Software" title="Special Command Software">Special Command Software</a>, if this is your first run dont worry about cargo bay extensions and engine tunings.
</p><p>Now command your freighter to move to <a href="/index.php/Kingdom_End" title="Kingdom End">Kingdom End</a>.
</p><p>Scout out Kingdom End, and bring a <a href="/index.php/Navigational_Beacon" title="Navigational Beacon">Navigational Beacon</a> or <a href="/index.php/Advanced_Satellite" title="Advanced Satellite" class="mw-redirect">Advanced Satellite</a> with you, preferably before your freighter gets there and move to these coordinates: X0,Y0,Z1.5, once there you should be right next to a few large Nividium rocks. Chances are you have something to blow them up but be careful not to blow up anything that is not Nividium, as your freighter may get confused and scoop that up too. Try not to move too much away from the coordinates because that is where you want to place your Navigational beacon or Satellite. 
</p><p>Another place that you can do this is in Ore Belt.  The first clue of the <a href="/index.php/Treasure_Hunt" title="Treasure Hunt">Treasure Hunt</a> quest will lead you to an astroid filled with Nividium, which you are required to break up in order to proceed.
</p><p>Now leave Kingdom End and order your Freighter to move to the Beacon or Satellite you placed. 
</p><p>Once the freighter arrives order it to collect rocks in the sector. It is imperative that you are not in sector as the AI collision detection can sometimes hose up your freighter and make this take longer.
</p><p>after about 30 minutes or so in SETA your Freighter should be full of Nividium, order it to dock at the nearest ship yard and sell the ship. using these funds go ahead and purchase more Freighters and rinse and repeat the process above. Don't stop at one freighter either buy 10 at a time and outfit them at the same time, then group order them to follow your Beacon, sadly once they arrive you will have to manually order each of them to collect rocks. A fully upgraded cargo mercury will take about 45 minutes to fill up, alternatively you can buy a XL mercury and out fit that. Once you get Boron Rep you can also buy a Super <a href="/index.php/Dolphin" title="Dolphin">Dolphin</a> with 14000 Cargo space, but you will have to order them to the EQ dock in <a href="/index.php/Three_Worlds" title="Three Worlds">Three Worlds</a> to get the ore collector (When selling the filled Dolphins you can trade out their eq to their replacements to save a trip).
</p><p>If your game has the XTC <a href="/index.php/Bonus_Pack" title="Bonus Pack">Bonus Pack</a> installed, you should aim for a more permanent setup, like this:
</p>
<ul><li>30 or so freighters (eg: Mistral) with <i>Special Command Software MK1</i>, <i>Ore Collector</i> and some cheap lasers (eg: IRE).</li>
<li>A TL class ship (eg: Argon Mammoth) parked in the same sector (in Kingdom End, it can be parked directly at the shipyard!).</li>
<li>A Super Freighter class (eg: Mistral SF) with Commodity Logistics Softwate MK2 and Navigation Command Software, homebased at the TL.</li></ul>
<p>The Mistrals will be collecting rocks continuously. The TL ship will be used as huge storage house.
The MSF will be carrying the ore from the collectors to the TL. Just order it to do external logistics: load ore at every mining ship and unload it at the TL. It should have 31 waypoints in total. So many waypoints can be handled by an expert trader only. Use more MSFs serving fewer collectors if your traders are less experienced.
Every now and then, check the TL at the shipyard. Buy 1 dolphin for every 2000 nividium in the TL's cargo hold, transfer the resources and sell them at 16M each!
For remote systems, the Super Freighter's External Logistics setup should be "Dock only if in same sector", to allow the TL to make the trip to the shipyard.
One last warning... so much money will take away all the fun.
</p>
<h2><span class="mw-headline" id="Nividium_and_the_PTNI_Stock_Exchange">Nividium and the PTNI Stock Exchange</span></h2>
<p>Another, even faster way of raising unlimited funds is using Nividium to manipulate the <a href="/index.php/Stock_Exchange" title="Stock Exchange">Stock Exchange</a> in <a href="/index.php/PTNI_Headquarters" title="PTNI Headquarters">PTNI Headquarters</a>.
</p><p>The price of a commodity traded at a stock exchange depends on the demand for the commodity in the sectors attached to that stock exchange, and the total number of units of that ware present in those sectors; the number of shares available appears to depend only on the latter. Interestingly, the amount of the ware present in the sectors <i>includes</i> wares on ships, which allows for easy manipulation of the share price.
</p><p>Normally, there are zero units of Nividium available in any of the sectors around PTNI Headquarters, but there are stations willing to buy a total of six units, so the price is at the maximum value of 24,192. Load up a transport with Nividium and fly it into, say, <a href="/index.php/Scale_Plate_Green" title="Scale Plate Green">Scale Plate Green</a>, East Gate. Now the supply (your transport full of Nividium) far exceeds the demand, so the share price drops to the minimum value of 8,064, and several thousand shares become available thanks to the large supply. Buy up all shares, move the transport out of the Stock Exchange's range, say, to <a href="/index.php/Nyana%27s_Hideout" title="Nyana's Hideout">Nyana's Hideout</a>, and the share price rises to the maximum again. Sell all shares, which will result in between several million up to several dozen million of profit, depending on how many units of Nividium you have on your transport and, thus, on how many shares had been available. The nice thing is that you keep the Nividium, so you can repeat until you hit the cap of 2 billion credits. With 3131 units of Nividium on your transport, each trade cycle makes pretty exactly 100 million credits in profit, and one trade cycle takes slightly over 1 minute, so you could hit the cap in about half an hour. The profit from Stock Exchange trades also increases your <a href="/index.php/Ranks" title="Ranks">Trade Rank</a>; with several thousand units of Nividium on your transport, you can reach Trade Rank: X-Treme in a matter of hours. You can also keep the transport docked in Scale Plate Green to be able to raise funds at a moment's notice if necessary.
</p><p>Of course, having utterly unlimited funds removes all challenge from the game, and thus all fun.
</p><p><br />
