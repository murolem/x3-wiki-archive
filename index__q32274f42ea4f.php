<h2><span class="mw-headline" id="Description">Description</span></h2>
<p>This sector is extremely crowded with asteroids. For a patient player willing to carefully tow their mines out of the dense thicket, this sector makes a good candidate for player complexes, as long as good relations with the Yaki to the south are maintained.
</p>
<h3><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span></h3>
<p>Increased <a href="/index.php/Kha%27ak" title="Kha'ak">Kha'ak</a> activity has meant that the <a href="/index.php/Paranid" title="Paranid">Paranid</a> have made no move to reclaim this inhospitable sector and no other race has shown interest as it borders only Paranid space. Localised <a href="/index.php/Yaki" title="Yaki">Yaki</a> activity is also a negative point to consider, leading some to believe it is used as a <a href="/index.php/Pirate" title="Pirate" class="mw-redirect">Pirate</a> or Yaki safe-haven.
</p>
