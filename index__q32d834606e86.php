<h3><span class="editsection">[<a href="/index.php?title=Ships&amp;action=edit&amp;section=1" title="Edit section: Class Groupings">edit</a>]</span> <span class="mw-headline" id="Class_Groupings">Class Groupings</span></h3>
<p>Ship classes are often grouped together to simplify description.
</p><p>The type of ships being used play the largest role in <a href="/index.php/Combat#Combat_strategies" title="Combat">combat strategy</a>, as fights between different classes of ships can become severely lopsided in straight contests of power.
</p><p>Combat ships are generally categorized into 2 different groups:
</p>
<ul><li><a href="/index.php/Category:Capital_Ships" title="Category:Capital Ships">Capital ships</a>
</li><li><a href="/index.php/Category:Fighters" title="Category:Fighters">Fighters</a>
</li></ul>
<p>A term coined here to describe combat ships not fitting the accepted groupings' descriptions is:
</p>
<ul><li><a href="/index.php/Category:Escort" title="Category:Escort">Escort</a>
</li></ul>
<p>Ships designed primarily to carry cargo can be described as:
</p>
<ul><li><a href="/index.php/Transports" title="Transports">Transports</a>
</li></ul>
<p>Ships who carry other ships can be described as:
</p>
<ul><li><a href="/index.php/Category:Carrier" title="Category:Carrier">Carriers</a>
</li></ul>
