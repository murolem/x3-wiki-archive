<div id="mw-content-text" lang="en" dir="ltr" class="mw-content-ltr"><p>Ships are vessels that transport goods and people between planets.<br />
In the <a href="/index.php/X-universe" title="X-universe">X-universe</a>, they usually employ a jumpgate network to rapidly transit to distant locations. This page serves as a hub for various information about these vessels.
</p>
