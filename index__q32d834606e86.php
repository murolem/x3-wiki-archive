<h3><span class="editsection">[<a href="/index.php?title=Ships&amp;action=edit&amp;section=3" title="Edit section: Ship Repair">edit</a>]</span> <span class="mw-headline" id="Ship_Repair">Ship Repair</span></h3>
<p>In the course of traveling the X-Universe, ships are often damaged, be it by weapons fire, hull-eating nebulae, or collisions.
</p>
<h4><span class="editsection">[<a href="/index.php?title=Ships&amp;action=edit&amp;section=4" title="Edit section: Ordinarily:">edit</a>]</span> <span class="mw-headline" id="Ordinarily:">Ordinarily:</span></h4>
<p>The most immediate solution is to exit the vessel and repair it with a <a href="/index.php/Repair_Laser" title="Repair Laser">Repair Laser</a> which is part of standard space suit equipment.
</p><p>The other option is to limp back to a <a href="/index.php/Shipyard" title="Shipyard">shipyard</a> for more extensive repairs.
</p><p>Equipment lost/destroyed to damage will have to be reacquired.
</p>
<h4><span class="editsection">[<a href="/index.php?title=Ships&amp;action=edit&amp;section=5" title="Edit section: Seta Repair:">edit</a>]</span> <span class="mw-headline" id="Seta_Repair:">Seta Repair:</span></h4>
<p>The player can eject the ship's <a href="/index.php/SETA" title="SETA" class="mw-redirect">SETA</a>, eject the ship as an astronaut, and collect the seta device. The player can then use SETA when out of ship.
</p><p>To repair quickly, use bore fire to fire the <a href="/index.php/Repair_Laser" title="Repair Laser">Repair Laser</a> while holding down the <a href="/index.php/SETA" title="SETA" class="mw-redirect">SETA</a> key.
</p><p>Notes:
</p>
<ul><li>SETA will not transfer back into the ship upon reentry.
</li><li>SETA will not stay in the astronaut suit forever. It effectively disappears upon entering a ship.
</li><li>If the player is unable to both fire the laser and use SETA at the same time, the player may need to bind SETA to a different key. E.g., Ctrl to fire and Alt to SETA fixed this issue.
</li><li>It is advised to use SETA repair near a shipyard or other dock at which the player can buy SETA. Otherwise, SETA is lost upon entering the ship, and it may take a long time to dock at a station that sells seta.
</li><li>Sometimes SETA will eject several KM away. Just fly to it in the ship and eject nearby.
</li><li>The astronaut suit has 2 hours of oxygen. SETA accelerates time. Keep this in mind. Using SETA 10x, the player can survive in space up to 12 minutes of real time.
</li></ul>
<h4><span class="editsection">[<a href="/index.php?title=Ships&amp;action=edit&amp;section=6" title="Edit section: With game modifications:">edit</a>]</span> <span class="mw-headline" id="With_game_modifications:">With game modifications:</span></h4>
<p>Repair Laser Factories can be added which will supply Repair Laser which can be mounted into certain ships i.e. <a href="/index.php/M6" title="M6">M6</a> or <a href="/index.php/TL" title="TL">TL</a> class ships.
</p>
<h3><span class="editsection">[<a href="/index.php?title=Ships&amp;action=edit&amp;section=7" title="Edit section: Shipyard Purchase Designations">edit</a>]</span> <span class="mw-headline" id="Shipyard_Purchase_Designations">Shipyard Purchase Designations</span></h3>
<p>Non-Capital ships sold from shipyards are designated with S, M, and L.
</p><p>These designations typically denote different equipment packages included in the sale. 
</p>
<ul><li>S model ships almost always are shielded with 1 MJ shields and are armed with IREs.  They are the "base model", and have few "extras", but are lower-priced if you want to equip them yourself. 
</li><li>M model ships are in an in-between point of S and L, often sporting weaker shields or incomplete shielding, and inferior equipment that you will probably want to sell as you outfit them yourself.
</li><li>L model ships by contrast are usually shielded with the maximum the class can hold and have heavier armaments, although not always the armaments you may want.  
</li></ul>
<p>Both M and L models may not be available, as their availability depends upon there being an <a href="/index.php/Equipment_Dock" title="Equipment Dock">Equipment Dock</a> with equipment in-stock to supply those ships. 
</p><p>As the details of each ship vary the most reliable way to determine differences is to look at the detail screens the shipyard posts. 
</p><p>Keep in mind that these differences are only there for convenience's sake.  You can always (and may frequently have to) outfit ships by transferring shields, upgrades, and weapons to them after purchase, and there is no advantage to buying an "L" model, other than having shields already added for you.  The game does not even remember what model you purchased past the point of purchase.
</p><p>You may likely wish to purchase only S model ships when you have <a href="/index.php/Stations" title="Stations">stations</a> of your own, or can easily purchase lower-priced weapons and shields.
</p>
