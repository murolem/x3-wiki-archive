<h2><span class="editsection">[<a href="/index.php?title=Phased_Repeater_Gun&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a>]</span> <span class="mw-headline" id="Overview">Overview</span></h2>
<p>The Phased Repeater Gun or PRG is a powerful heavy fighter weapon used on <a href="/index.php/Argon" title="Argon">Argon</a> ships amongst others. Not to be confused with the <a href="/index.php/Plasma_Burst_Generator" title="Plasma Burst Generator">PBG</a> or <a href="/index.php/Phased_Shockwave_Generator" title="Phased Shockwave Generator">PSG</a>.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Phased_Repeater_Gun&amp;action=edit&amp;section=2" title="Edit section: Encyclopedia Entry">edit</a>]</span> <span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span></h2>
<p>A weapon found in some medium and large fighters.
</p><p>Designed for M3 fighters to be lighter and faster than HEPTs, and also serving as a heavier weapon for M4s. The Phased Repeater Gun fires in quick bursts of 3 rounds and is ideal for taking out fast and hard-to-hit enemy fighters.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Phased_Repeater_Gun&amp;action=edit&amp;section=3" title="Edit section: Commentary">edit</a>]</span> <span class="mw-headline" id="Commentary">Commentary</span></h2>
<p>The PRG is a light and fast weapon. The high shell velocity makes it a very accurate weapon and it deals a decent amount of damage, making its destructive potential against fighters very high. A good weapon for an M3 that has to deal with smaller adversaries that are too difficult to hit with the <a href="/index.php/High_Energy_Plasma_Thrower" title="High Energy Plasma Thrower">HEPT</a>. An excellent choice for back/front turrets on Commonwealth fighters capable of mounting and powering it.
</p>
