<h2><span class="editsection">[<a href="/index.php?title=Ion_Cannon&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a>]</span> <span class="mw-headline" id="Overview">Overview</span></h2>
<p>The Ion Cannon, sometimes abbreviated to IC, is a <a href="/index.php/Boron" title="Boron">Boron</a> capital ship weapon that does minimal hull damage but massive damage to shields and causes many other side-effects.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Ion_Cannon&amp;action=edit&amp;section=2" title="Edit section: Encyclopedia Entry">edit</a>]</span> <span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span></h2>
<p>A deadly weapon found on frigates, destroyers and carriers. Upscaling IPG technology and taking advantage of the massive energy available to capital ships, the Ion Cannon is the pinnacle of Boron ion weaponry. A huge amount of ion energy is concentrated into a single projectile and can disrupt a target ship's systems, such as engines and weapons, in addition to causing a large reduction in shield energy.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Ion_Cannon&amp;action=edit&amp;section=3" title="Edit section: Commentary">edit</a>]</span> <span class="mw-headline" id="Commentary">Commentary</span></h2>
<p>An unusual capital weapon with the highest shield damage rate in the game, the Ion Cannon deals a large variety of hidden effects, such as energy draining, high chance of equipment damage, speed reduction and suchlike. Its effectiveness against hulls is poor, leading it to be used in conjunction with the <a href="/index.php/Gauss_Cannon" title="Gauss Cannon">Gauss Cannon</a> to cause massive damage on both sides of enemy defence.
</p>
