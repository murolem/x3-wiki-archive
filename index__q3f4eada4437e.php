<a name="Overview" id="Overview"></a><h2> <span class="mw-headline">Overview</span></h2>
<a name="Background" id="Background"></a><h2> <span class="mw-headline">Background</span></h2>
<p>The original Elite served the <a href="/index.php/Argon" title="Argon">Argon Federation</a> for many years as an <a href="/index.php/M3" title="M3">M3</a> class fighter. However, following the victory over the <a href="/index.php/Xenon" title="Xenon">Xenon</a> <a href="/index.php?title=M0&amp;action=edit&amp;redlink=1" class="new" title="M0 (page does not exist)">M0</a> mothership and subsequent rout of Xenon forces from Commonwealth space, it was retired and replaced with the <a href="/index.php/Nova" title="Nova">Nova</a>.
</p><p>Some years later, with the threats posed by the <a href="/index.php/Kha%27ak" title="Kha&#39;ak">Kha'ak</a> and the new political and military equilibrium being found due to the connection of the <a href="/index.php/Terran" title="Terran">Solar System</a> into the jumpgate network, the designs for the old Elite were pulled out of storage, reworked and updated with modern technology, and the ship re-entered production as the Argon light fighter.
</p><p><br />
</p>
