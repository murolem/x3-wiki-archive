I'm looking at the Shuri and Kha'ak Hive Queen and wondering if they should be on this list. I know that they're M1 class vessels but they're pretty much indistinct from the Colossus and Kha'ak Carrier respectively. Of course they should have their own articles, but I think that these articles should be linked to from the Colossus &amp; Hoshi and the Kha'ak Carrier articles respectively. I don't think that the ship lists, which I see being used to compare ships, should be cluttered with near-carbon-copies of other ships. Unless anyone raises an objection, I'll carry out the aforementioned edits in a few days. [[User:Spychotic|Spychotic]] 16:40, 8 October 2010 (CDT)

perhaps if they are effectively variants then they should be merged into a master page and redirect the other article to that master? The information on one if they are copies ought to apply to the other and having it replicated more or less verbatim is counter productive. On the downside, if the values do differ in some future patch you're left, at a minimum, with recreating the variant infobox. 

In the case of the shuri, having a separate entry for a different faction's version does make a little sense as then you can categorize the page separately. I am considering splitting off the pirate versions of the nova and falcon for example, they have clear connections to the main article, but do deserve mention as being different in that they were stolen/repurposed/hijacked.
