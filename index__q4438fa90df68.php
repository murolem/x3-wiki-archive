The definition of "Major" and "Minor" races is a bit blurry but this is what I've decided on. Roughly, 'Major' races are ones you have distinct rep with that can be gained and lost in the usual way and have relatively full ship lists and 'Minor' races depend on another's rep and/or have relatively few different ship types.

I've also removed the Kha'ak Guardian from the list as it's an orbital defence platform rather than a carrier, and I've removed the Mobile Repair Ship because, to my knowledge, it's not in any vanilla game. A more appropriate place for this would be a special 'Non-Vanilla Ships' section, were it kept here then I for one would be very annoyed at having my hopes lifted seeing it alongside the other M1s only to have them dashed by being told it's not available!

I might move the ATF Woden there for the same reason. I'm creating the section now. [[User:Spychotic|Spychotic]] 05:11, 6 October 2010 (CDT)

