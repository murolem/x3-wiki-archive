<h2><span class="editsection">[<a href="/index.php?title=Return_Ship&amp;action=edit&amp;section=1" title="Edit section: Objective">edit</a>]</span> <span class="mw-headline" id="Objective">Objective</span></h2>
<p>Return an abandoned drifting ship to the client within a set time period.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Return_Ship&amp;action=edit&amp;section=2" title="Edit section: General difficulty">edit</a>]</span> <span class="mw-headline" id="General_difficulty">General difficulty</span></h2>
<p>Easy in some cases, very hard in other cases. 
</p>
<h2><span class="editsection">[<a href="/index.php?title=Return_Ship&amp;action=edit&amp;section=3" title="Edit section: What you will need">edit</a>]</span> <span class="mw-headline" id="What_you_will_need">What you will need</span></h2>
<ul><li> A fast way to get to the ship, claim it and get back
</li></ul>
<h3><span class="editsection">[<a href="/index.php?title=Return_Ship&amp;action=edit&amp;section=4" title="Edit section: Useful Tools">edit</a>]</span> <span class="mw-headline" id="Useful_Tools">Useful Tools</span></h3>
<ul><li> A fast <a href="/index.php/M5" title="M5">M5</a> or a ship with <a href="/index.php/Jumpdrive" title="Jumpdrive">Jumpdrive</a> and <a href="/index.php/Energy_Cells" title="Energy Cells">Energy Cells</a>
</li><li> <a href="/index.php/System_Override_Software" title="System Override Software">System Override Software</a> is useful but illegal. 
</li><li> <a href="/index.php/TM" title="TM">TM</a> class ship with Jumpdrive and Energy Cells to transport fighters quickly.
</li><li> A ship equipped with <a href="/index.php/Tractor_Beam" title="Tractor Beam">Tractor Beam</a> - Can be used to tow large slow-moving ships (TS, M7 or M8) to their stations slightly faster than their maximum speed.
</li></ul>
<h2><span class="editsection">[<a href="/index.php?title=Return_Ship&amp;action=edit&amp;section=5" title="Edit section: What you will find">edit</a>]</span> <span class="mw-headline" id="What_you_will_find">What you will find</span></h2>
<p>In the beginning of the game maybe a small <a href="/index.php/M5" title="M5">M5</a> or <a href="/index.php/M4" title="M4">M4</a> fighter. Later on you can get as much as an <a href="/index.php/M8" title="M8">M8</a> or <a href="/index.php/M7" title="M7">M7</a>.
</p><p>Oftentimes the ship has few or no speed upgrades so it's advantageous to have a carrier for smaller ships and a tug for larger ships to hurry them along.  Jumpdrives are invaluable to getting the target vessel back to the return station's sector and can often compensate for poor speed.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Return_Ship&amp;action=edit&amp;section=6" title="Edit section: Commentary and strategy">edit</a>]</span> <span class="mw-headline" id="Commentary_and_strategy">Commentary and strategy</span></h2>
<p>This is a simple 'retrieve' mission you will need to do to find a ship, claim it and send it back to it's owner. However there is a twist to this mission favoured by many X-Gamers. You can gain by claiming it and NOT sending it back. The downside of keeping the ship in one of these missions is that you take a big rep loss, because it's considered stealing. 
</p><p>There are many ways to suffer rep loss on this mission:
</p>
<ul><li>Aborting the mission - rep loss, plus the ship will jump away if you haven't claimed it. 
</li><li>Shoot it down - rep loss. 
</li><li>Crash into it and blow it up - rep loss. 
</li><li>Keep ship - rep loss.
</li></ul>
<p>By contrast there are only a few ways to avoid losing rep on this mission:
</p>
<ul><li>Successfully complete the mission.
</li><li>Have ship destroyed by someone else - and you will receive a small sum of money and no rep loss! 
</li><li>Self Destruct will have the same effect as having it destroyed by hostiles. A curious quirk!
</li></ul>
<p>So choose this mission only if you seriously plan to finish it or plan to keep the ship and suffer the consequences. Sometimes the ship is worth more than the mission reward.
</p><p>Note that failing to deliver it back on time (whether voluntarily or involuntarily) after claiming the ship causes some sector police to spawn dedicated to destroying it. They will continue to try until the ship is somehow destroyed, meaning that these sector police require eliminating with the full repercussions to your race rep of doing so. By luring the police through a <a href="/index.php/Xenon" title="Xenon">Xenon</a> sector using the ship's jumpdrive, this problem can be got round quite easily.
Alternately, if you are friendly with the Yaki, dock the ship at the Shipyard in Senator's Badlands; the Yaki patrols will eventually take care of the pursuers.
</p><p>Note: You can be offered return ship missions for M7 ships from a station where they cannot dock. In such circumstances, simply moving the ship close to the client's station will complete the mission.
</p>
