<h2><span class="mw-headline" id="Overview">Overview</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Ore&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>One of the most common minerals through out the whole known X-Universe, it is used in the production process of almost all technical goods. Ore is produced in <a href="/index.php/Ore_Mine" title="Ore Mine">Ore Mines</a> which are built around small asteroids.
</p><p>Ore is available in almost every Sector.
</p>
