<h2><span class="editsection">[<a href="/index.php?title=Atmospheric_Lifter&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a>]</span> <span class="mw-headline" id="Overview">Overview</span></h2>
<p>One of the more sought after TLs, the Atmo. Lifter sports the second (or third) largest cargo bay of any ship in the game, at 80,000 units. It is pretty fast, making it pretty much ideal for station hauling or storage. It is large, however, and has trouble docking or even passing through gates.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Atmospheric_Lifter&amp;action=edit&amp;section=2" title="Edit section: Background">edit</a>]</span> <span class="mw-headline" id="Background">Background</span></h2>
<h2><span class="editsection">[<a href="/index.php?title=Atmospheric_Lifter&amp;action=edit&amp;section=3" title="Edit section: Physical Characteristics">edit</a>]</span> <span class="mw-headline" id="Physical_Characteristics">Physical Characteristics</span></h2>
<h2><span class="editsection">[<a href="/index.php?title=Atmospheric_Lifter&amp;action=edit&amp;section=4" title="Edit section: Combat">edit</a>]</span> <span class="mw-headline" id="Combat"> Combat </span></h2>
<p>The AL has no guns, and is limited to defending itself with a small selection of missiles. Not good in a fight.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Atmospheric_Lifter&amp;action=edit&amp;section=5" title="Edit section: Equipment">edit</a>]</span> <span class="mw-headline" id="Equipment"> Equipment </span></h2>
<h2><span class="editsection">[<a href="/index.php?title=Atmospheric_Lifter&amp;action=edit&amp;section=6" title="Edit section: Availability">edit</a>]</span> <span class="mw-headline" id="Availability"> Availability </span></h2>
<h2><span class="editsection">[<a href="/index.php?title=Atmospheric_Lifter&amp;action=edit&amp;section=7" title="Edit section: Suggested Roles">edit</a>]</span> <span class="mw-headline" id="Suggested_Roles"> Suggested Roles</span></h2>
<p>Because the Atmospheric Lifter doesn't fit through gates, it is usually used as a floating storage dump, making use of its vast cargo capacity.
</p>
