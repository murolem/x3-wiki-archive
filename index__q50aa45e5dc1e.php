<h2><span class="mw-headline" id="Objective">Objective</span></h2>
<p>Acquire and deliver a designated ship spaceframe.
</p>
<h2><span class="mw-headline" id="General_Difficulty:">General Difficulty:</span></h2>
<p>Easy/Medium.
</p>
<h2><span class="mw-headline" id="What_you_will_need:">What you will need:</span></h2>
<ul>
<li> Cash enough to buy the ship.
</li>
<li> If the ship they are looking for is far away you might want a spare <a href="/index.php/Jumpdrive" title="Jumpdrive">jumpdrive</a> and energy cells to load onto the ship when you buy it to quickly jump it to its new owner, or a <a href="/index.php/TM" title="TM">TM</a> with jumpdrive to pick up the ship and jump it back to its new owner. 
</li>
</ul>
<h2><span class="mw-headline" id="What_you_will_find:">What you will find:</span></h2>
<ul>
<li> The ship to be delivered will typically be a <a href="/index.php/Category:Fighters" title="Category:Fighters">fighter</a>, but could be an <a href="/index.php/M8" title="M8">M8</a> or <a href="/index.php/M6" title="M6">M6</a> class ship.
</li>
</ul>
<h2><span class="mw-headline" id="Commentary_and_Strategy">Commentary and Strategy</span></h2>
<p>To complete this mission, get the requisite ship to the target and a message will pop up asking you to turn the ship over to the new owner. It will leave your control at this point.
</p><p>These missions tend to be very tight for time. You don't have time to dawdle and in many cases even flying the fully-upgraded ship directly to the target from the shipyard isn't physically possible given the timeframe. This is why it is recommended that a spare Jumpdrive or a fast TM with a jumpdrive is kept on hand for these missions.
</p><p>The only requirement is to deliver the spaceframe, not any equipment. As such, don't waste time fitting it out with anything other than the engine and rudder tunigs that will get it to its destination. Note that equipping the ship does not eat into your profit margin, as you'll be reimbursed the ship cost in addition to picking up your reward.
</p><p>In some cases the mission will ask you to deliver a ship to a shipyard that's selling the ship. So buy and instant cash!
</p><p><b>Warning:</b> Sometimes there will be two variations of a ship with both exactly the same name and it is impossible to know which one to deliver. So if the mission does not complete when you deliver a ship it could be because you have delivered the wrong one!
</p><p><b>Warning:</b> It is possible to get asked to deliver ships that are not actually sold at any shipyards.
</p><p><br />
</p>
