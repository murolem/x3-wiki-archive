In X-BTF, unlike each subsequent game, the player was confined to one ship, the [[X-perimental Shuttle|X-Shuttle]]. The X-shuttle began with no weapons in either of its two weapon slots, a single 1MJ shield in its two shield slots and very limited cargo capacity, speed and manoeuvrability. However, there was no limit to the number or upgrades it could recieve and it was capable of assimilating every piece of equipment available.

This equipment came in the form of the [[SETA]], a number of computer modules that added autopiloting capabilities to the ship, fire control systems and more. Many of these would come as standard on ships in later games.

===Map===
There were only 54 Sectors in the original X-Universe [http://www.nappefugl.dk/index/map.html map], with many well-known sectors from later games yet to be discovered and the neutral bloc between what would come to be known as [[Farnham's Legend]] and [[Hatikvah's Faith]] under Xenon control.

===Ships===
The game introduced the original seven classes of ship; the military classes from [[M1]] through [[M5]] and the [[TS]] and [[TL]] classes.

===Economy===
The X-BTF economy is much the same as in later games. No new basic wares (but many weapons and munitions) have been added since. Factory management was much simpler, with factories behaving much as NPC factories do. Ships could only be bought at factories and could not be reassigned from that factory. The biggest difference was the cost of wares; Credits were divided into decimals and the prices were approximately one-quarter of those common in the later game.

===Weapons===
There were nine weapons, five missiles and four shields available in the game. The IRE, PAC and HEPT were each divided into Alpha, Beta and Gamma variants, with the Alpha IRE being the weakest weapon, found on light scouts, and the Gamma HEPT being the strongest and found only on [[:Category:Capital Ships|capital ships]] and, late in the game, the X-Shuttle. All fighters were equipped with only two weapons, IREs for light fighters and PACs for heavy fighters. Missiles all behaved the same way, single-shot homing warhards, with heavier missiles trading speed for power. The five available missiles were (in order of power) the Mosquito, Wasp, Dragonfly, Silkworm and Hornet. Shields available ranged in size from 1MW through 5MW, 25MW and 125MW and each ship carried two, the size depending on the class.
