</p>
<h3><span class="editsection">[<a href="/index.php?title=Universe_Traders_Guide&amp;action=edit&amp;section=10" title="Edit section: Satellite Network">edit</a>]</span> <span class="mw-headline" id="Satellite_Network"> Satellite Network </span></h3>
<p>As Universe Traders require seeing the best prices around the Universe to get the best deals, it is usually seen to be the best policy to set up a satellite network covering as large an area as possible. While you can try and reveal the entirety of every sector for your own peace of mind, typically one <a href="/index.php/Satellite" title="Satellite">satellite</a> (of either kind) per sector is sufficient.
</p><p>Of course, this also means exploring as many sectors as possible to allow your Universe Traders to get the best deals.
</p><p><br />
Please note that <a href="/index.php/Advanced_Satellite" title="Advanced Satellite" class="mw-redirect">Advanced Satellites</a> could be seen as the more desirable choice following the installation of the <a href="/index.php/Bonus_Pack" title="Bonus Pack">Bonus Pack</a>, due to its addition of the <a href="/index.php/Satellite_Early_Warning_Network" title="Satellite Early Warning Network" class="mw-redirect">Satellite Early Warning Network</a>.
</p>
<h3><span class="editsection">[<a href="/index.php?title=Universe_Traders_Guide&amp;action=edit&amp;section=11" title="Edit section: Bonus Pack">edit</a>]</span> <span class="mw-headline" id="Bonus_Pack"> <a href="/index.php/Bonus_Pack" title="Bonus Pack">Bonus Pack</a> </span></h3>
<p>The Bonus Pack includes several useful <a href="/index.php/Scripts" title="Scripts" class="mw-redirect">Scripts</a> for using Mk3 Traders, including the <a href="/index.php?title=Mk3_Blacklist_Manager&amp;action=edit&amp;redlink=1" class="new" title="Mk3 Blacklist Manager (page does not exist)">Mk3 Blacklist Manager</a> - a feature where the player can specify sectors that the Universe Trader should not visit. An absolute must in Albion Prelude to prevent UT's falling foul of the War Sectors.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Universe_Traders_Guide&amp;action=edit&amp;section=12" title="Edit section: Bugs">edit</a>]</span> <span class="mw-headline" id="Bugs"> Bugs </span></h2>
<p><i>For a more in-depth look at fixing bugs, please take a look through several of the links provided at <a href="#See_Also">the bottom of the page</a>.</i>
</p><p>Common problems include:
</p>
<h3><span class="editsection">[<a href="/index.php?title=Universe_Traders_Guide&amp;action=edit&amp;section=13" title="Edit section: Trader Not Doing Anything">edit</a>]</span> <span class="mw-headline" id="Trader_Not_Doing_Anything"> Trader Not Doing Anything </span></h3>
<p><b>Cause:</b> It depends. It can come under one of two guises:
</p>
<ul><li><i>Standby:</i> No profitable trades found.
</li><li><i>Idle:</i> means there is an issue that ought to resolve itself soon - e.g. enemies present, or missing pre-requisite. It also covers generic problems which you (the player) may have to fix. Be careful of this one, as it could be the trading bay is full of an item it can't sell, and will need to be manually emptied, or you might need to restart trading from a different sector. If you notice Traders often end up in falling to "Idle" in one sector, consider adding that sector to your Mk III Trader Blacklist via the in-game "Additional Commands" accessible on any ship with <a href="/index.php/Trade_Command_Software#Trade_Command_Software_Mk3" title="Trade Command Software">Mk III Trading Software</a> installed.
</li></ul>
<p><br />
Often it will be that they buy a good, attempt to sell it, and are beaten to the sale, resulting in a full hold of an item they can't find anywhere else to sell. Other times would be if their hold can't take the trade they want to include (e.g. not XL-capable; may have changed since bug was originally reported in Reunion).
</p>
<h3><span class="editsection">[<a href="/index.php?title=Universe_Traders_Guide&amp;action=edit&amp;section=14" title="Edit section: Trader Not Working Properly">edit</a>]</span> <span class="mw-headline" id="Trader_Not_Working_Properly"> Trader Not Working Properly </span></h3>
<p><b>Cause:</b> This bug is also pretty broad. The most common fault is conflicting scripts. If you've got the same ship being attempted to be managed by other modifications, or it transferred badly from one job to another, consider taking a look at it through The <a href="/index.php/Script_Editor" title="Script Editor" class="mw-redirect">Script Editor</a>.
</p>
<h3><span class="editsection">[<a href="/index.php?title=Universe_Traders_Guide&amp;action=edit&amp;section=15" title="Edit section: Common Fixes">edit</a>]</span> <span class="mw-headline" id="Common_Fixes"> Common Fixes </span></h3>
<p>Regardless of what went wrong, things to attempt to fix broken Mk3 Traders:
</p>
<ul><li> Restart in different sector.
</li><li> Restart when docked.
</li><li> Kill nearby enemies.
</li><li> Make sure your <a href="/index.php/Navigation_Relay_Satellite" title="Navigation Relay Satellite">Navigation Relay Satellite</a> Network is functional.
</li><li> Check the information on the ship (under player's properties - default shortcut is "R"), immediately after ordering Start... Trader. It should flash through available items to buy/sell before going to "Standby".
</li><li> Check Friend/Foe settings.
</li><li> Check for sufficient energy for jump.
</li></ul>
<p><br />
</p>
