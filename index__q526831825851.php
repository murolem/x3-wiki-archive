<div id="mw-content-text" lang="en" dir="ltr" class="mw-content-ltr"><p><i>See Also: <a href="/index.php/Traders" title="Traders">Traders</a></i><br />
</p><p>Universe and Sector Traders are one of the main ways to make money in x3, whether it be <a href="/index.php/Reunion" title="Reunion" class="mw-redirect">Reunion</a>, <a href="/index.php/Terran_Conflict" title="Terran Conflict" class="mw-redirect">Terran Conflict</a> or <a href="/index.php/Albion_Prelude" title="Albion Prelude" class="mw-redirect">Albion Prelude</a>.
</p><p>When managed correctly, they will often make more money than a factory for the same cost, and can run away when necessary; but don't let this fool you - using <a href="/index.php/Simulation_Modelling" title="Simulation Modelling">OOS Combat Resolution</a> will still mean that they will die both quickly and easily if they get into a fight.
</p>
