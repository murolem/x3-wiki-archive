<h2><span class="editsection">[<a href="/index.php?title=Universe_Traders_Guide&amp;action=edit&amp;section=1" title="Edit section: Why use Universe or Sector Traders?">edit</a>]</span> <span class="mw-headline" id="Why_use_Universe_or_Sector_Traders.3F"> Why use Universe or Sector Traders? </span></h2>
<p>This is an often asked question, but also one of the simplest. It's one of the easiest ways to effortlessly make money.
</p><p>After you have got your Sector Trader up to being a Universe Trader, there is very little you will need to do to maintain him (at least in theory - see <a href="#Bugs">Bugs</a> below for a list of known bugs and how to solve them). They often make more than most factories for the same price, and while unlike factories, they cannot be used directly for equipping your own fleet, their autonomy is their greatest strength.
</p><p>However, they can stumble into trouble, and if you are not careful, you could get unlucky and loose one - particularly if you are playing in Albion Prelude, and do not set up things like your Sector Blacklist, Friend/Foe etc. correctly.
</p><p>Please note that a popular alternative to a Universe Trader is a ship properly equipped with <a href="/index.php/Commodity_Logistics_Software" title="Commodity Logistics Software">Commodity Logistics Software</a>, and while that is not covered within the scope of this guide, it is an option worth considering, if you understand how it works. Especially if you want your trader to run double-duty and also prioritize selling your own goods.
</p><p><br />
</p>
<h2><span class="editsection">[<a href="/index.php?title=Universe_Traders_Guide&amp;action=edit&amp;section=2" title="Edit section: Starting Out (Sector Traders)">edit</a>]</span> <span class="mw-headline" id="Starting_Out_.28Sector_Traders.29"> Starting Out (Sector Traders) </span></h2>
<p>Before starting out as a Universe Trader, the ship will need several pieces of equipment, and must meet several requirements prior to becoming a Universe Trader. Firstly, the ship must be equipped with <a href="/index.php/Trade_Command_Software#Trade_Command_Software_Mk3" title="Trade Command Software">Trade Command Software Mk3</a>.
</p><p>Starting Universe Traders is as simple as getting a Trader of a high enough rank, and ordering him to "Begin Universe Trading". After doing that, you ought to be able to largely forget about them - meaning that the majority of the work is put in while they are a Sector Trader, as such, while this guide is aimed at the long-run (thinking about getting Universe Traders), the beginning is aimed primarily at Sector Traders - as you cannot have a Universe Trader without first having a Sector Trader.
</p>
<h3><span class="editsection">[<a href="/index.php?title=Universe_Traders_Guide&amp;action=edit&amp;section=3" title="Edit section: Required Ship Class">edit</a>]</span> <span class="mw-headline" id="Required_Ship_Class"> Required Ship Class </span></h3>
<ul><li>Ship type must be one of:
<ul><li><a href="/index.php/M4" title="M4">M4 Interceptor</a>
</li><li><a href="/index.php/M3" title="M3">M3 Fighter</a> // <a href="/index.php/M3%2B" title="M3+">M3+ Heavy Fighter</a>
</li><li><a href="/index.php/M6" title="M6">M6 Corvette</a> // <a href="/index.php/M6%2B" title="M6+">M6+ Heavy Corvette</a>
</li><li><a href="/index.php/TS" title="TS">TS Transport</a> // <a href="/index.php/TS%2B" title="TS+">TS+ Heavy Transport</a>
</li><li><a href="/index.php/TM" title="TM">TM Military Transport</a>
</li><li><a href="/index.php/TP" title="TP">TP Personnel Transport</a>
</li></ul>
</li><li>(Unconfirmed) Must be able to carry <a href="/index.php/Cargo_Class" title="Cargo Class">XL</a>-class <a href="/index.php/Wares" title="Wares">wares</a>.
</li></ul>
<p>Please see below for a list of <a href="#Recommended_Ships">Recommended Ships</a>.
</p>
<h3><span class="editsection">[<a href="/index.php?title=Universe_Traders_Guide&amp;action=edit&amp;section=4" title="Edit section: Training">edit</a>]</span> <span class="mw-headline" id="Training"> Training </span></h3>
<p>After you equip a ship from the above classes with the Trade Command Software, you will be able to issue the order of "Start Sector Trader". This will hire a pilot who will begin trading on your behalf - buying wares in the sector you select for a good price, and selling them to other stations for a good price.
</p><p><br />
The most important asset here appears to be the number of trades, not the quantity of items transferred, nor the total profit made. In the past, this meant that using a smaller, faster ship was preferable to train up your pilot, before using <a href="/index.php/Special_Command_Software" title="Special Command Software">Special Command Software</a> to transfer them to a more favourable ship.
</p>
<h3><span class="editsection">[<a href="/index.php?title=Universe_Traders_Guide&amp;action=edit&amp;section=5" title="Edit section: Pilot Levels">edit</a>]</span> <span class="mw-headline" id="Pilot_Levels"> Pilot Levels </span></h3>
<p><i>Main article: <a href="/index.php/Traders#Pilot_Levels" title="Traders">Traders#Pilot Levels</a></i><br />
As Pilots level up, they gain more abilities. The most important ranks to pay attention to are:
</p>
<ul><li> <i>Rank 6</i> - Will use Sector Trader to trade up to 1 sector away.
</li><li> <i>Rank 7</i> - Actually checks if it can make a profit.
</li><li> <i>Rank 8</i> - Universe Trader, use <a href="/index.php/Jumpdrive" title="Jumpdrive">Jumpdrive</a>, buy <a href="/index.php/Fighter_Drones" title="Fighter Drones" class="mw-redirect">Fighter Drones</a>
</li><li> <i>Rank 12</i> - Buys own Jumpdrive.
</li></ul>
<h2><span class="editsection">[<a href="/index.php?title=Universe_Traders_Guide&amp;action=edit&amp;section=6" title="Edit section: Recommendations">edit</a>]</span> <span class="mw-headline" id="Recommendations"> Recommendations </span></h2>
<p>Below are a list of basic recommendations. Feel free to do whatever you want, unlike the information above, this isn't <i>binding</i> within the game, but most of it is a mixture of common sense and experience, so if you're doing something differently, perhaps consider if it really is the best way to do things?
</p>
