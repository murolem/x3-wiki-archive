<h2><span class="mw-headline" id="Overview">Overview</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Computer_Plant&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>Computer Plants manufacture <a href="/index.php/Computer_Components" title="Computer Components">Computer Components</a> out of <a href="/index.php/Energy_Cells" title="Energy Cells">Energy Cells</a>, <a href="/index.php/Silicon" title="Silicon" class="mw-redirect">Silicon</a> and <a href="/index.php/Food" title="Food" class="mw-redirect">Food</a>.
</p><p>NPC Computer Plants also require <a href="/index.php/Microchips" title="Microchips">Microchips</a>, hence these factories will often be sited near a <a href="/index.php/Chip_Plant" title="Chip Plant">Chip Plant</a>.
</p>
<h3><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Computer_Plant&amp;action=edit&amp;section=2" title="Edit section: Encyclopedia Entry">edit</a><span class="mw-editsection-bracket">]</span></span></h3>
<p>A Computer Plant manufactures both full computer systems and individual computer components that are in great demand throughout the entire universe for general use.
</p><p>Factories also create the more specialised computer systems found in all space ships and weapon systems. 
</p>
