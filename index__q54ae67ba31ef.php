<h2><span class="mw-headline" id="Overview">Overview</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Electro-Magnetic_Plasma_Cannon&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>The standard weapon for all <a href="/index.php/Terran" title="Terran">Terran</a> fighters, and the least potent Terran weapon. Very expensive and fairly powerful for its class.
</p>
<h2><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Electro-Magnetic_Plasma_Cannon&amp;action=edit&amp;section=2" title="Edit section: Encyclopedia Entry">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>A weapon mostly found on some medium to larger-sized fighter craft. The Earth Fleet's powerful and highly advanced Electro-Magnetic Plasma Cannon is the most common weapon found on Terran fighters. It uses powerful EM field generators to propel concentrated plasma bolts at enemies.
</p>
<h2><span class="mw-headline" id="Commentary">Commentary</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Electro-Magnetic_Plasma_Cannon&amp;action=edit&amp;section=3" title="Edit section: Commentary">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>The EMPC is both the lightest Terran gun and the sole gun permitted to Terran and ATF fighters, and is almost exclusively found mounted on Terran or Xenon ships. It is also the sole weapon mounted on the <a href="/index.php/Keris" title="Keris">Keris</a> drone.
</p><p>The EMPC features projectile speed between the <a href="/index.php/Particle_Accelerator_Cannon" title="Particle Accelerator Cannon">PAC</a> and <a href="/index.php/High_Energy_Plasma_Thrower" title="High Energy Plasma Thrower">HEPT</a>, shield DPS 13.33% below the PAC, hull DPS 33.33% above the HEPT, and range 0.08km above the PAC. Additionally, it touts approximately half the RoF of the HEPT, less than half the energy usage of a HEPT, and costs a bit over twice as much as a HEPT.
</p><p>The EMPC is very efficient (and fairly powerful) against heavily hulled ships, and the lower shield damage is typically negligible versus light opponents. Against fast ships like <a href="/index.php/M5" title="M5">M5 Scouts</a> the EMPC will prove somewhat more accurate than a HEPT, but the lower volume of fire will result in less frequent hits. A light vessel when struck by the EMPC's shots will often be crippled by the heavy hull damage.
</p><p>The primary weaknesses of this armament are that it is the sole Terran fighter gun, the firing rate is comparatively sub-par, shots are relatively slow-moving which correlates to poor DPS, and that it is the most expensive/slowest to produce/hardest to source of any fighter gun. <a href="/index.php/Commonwealth" title="Commonwealth">Commonwealth</a> ships in contrast typically have a variety of lighter, cheaper weapons ideal for engaging fast targets like the <a href="/index.php/IRE" title="IRE" class="mw-redirect">IRE</a>, <a href="/index.php/PRG" title="PRG" class="mw-redirect">PRG</a> and <a href="/index.php/PBE" title="PBE" class="mw-redirect">PBE</a>, or more powerful weapons like the <a href="/index.php/EBC" title="EBC" class="mw-redirect">EBC</a> and <a href="/index.php/HEPT" title="HEPT" class="mw-redirect">HEPT</a>. In essence, Terran and ATF craft below <a href="/index.php/M6" title="M6">M6</a> corvettes must get within point-blank range to close the distance with its opponents when utilizing the EMPC and it will prove quite frustrating when attempting to dogfight with <a href="/index.php/M5" title="M5">M5</a> and <a href="/index.php/M4" title="M4">M4</a> craft thanks to the gun's aforementioned weaknesses.
</p><p>The standard version of the EMPC is cheaper, more powerful and shoots more rapidly than the (Aldrin) <a href="/index.php/EEMPC" title="EEMPC" class="mw-redirect">Experimental version</a>.
</p>
