<h2><span class="mw-headline" id="Note">Note</span></h2>
<ol><li>M8s and M7Ms are not normally included in "All" because they have a "Specific" missile. (Besides Mosquito)</li>
<li>M5s are not usually included in "All" and normally use Mosquito missiles or low-yield Terran missiles.</li>
<li>Commonwealth includes Yaki and Xenon, since they do not have specific race-based missiles. However, Xenons supports almost every single missile in the game besides Kha'ak missiles.</li>
<li>the "Producing race = none" means that it's super rare and is not producible by ANY factory.</li>
<li> Blank "Producing race" means that the Commonwealth make the missile.</li></ol>
