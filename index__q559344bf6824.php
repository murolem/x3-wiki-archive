<h2><span class="mw-headline" id="Overview">Overview</span></h2>
<p>Missiles are expendable munitions.
</p><p>Most attack missiles and torpedoes detonate to cause damage to shields and hulls.  Their primary purpose, to distinguish themselves from energy <a href="/index.php/Weapons" title="Weapons">weapons</a>, is that they consume much more space and functionally cost you money to attack, but compensate by being capable of inflicting far more damage in a given period of time than a regular weapon can.  A good many types of missiles cannot be bought at all, and must be hoarded from enemy kills, making them more precious as a weapon, or else simply sellable loot from a kill.  Many powerful missiles and torpedoes have ranges far greater than normal lasers, making them capable of being used from "safe" ranges where a smaller ship would be vaporized by capital ship fire if they traveled into standard weapons range.  
</p><p>This means missiles have a large role in <a href="/index.php/Combat#Combat_strategy" title="Combat">combat strategy</a>, as it gives weaker ships a chance to kill stronger ships, if they are willing to sacrifice money or rare loot, or simply have the <a href="/index.php/Stations" title="Stations">missile complexes</a> to buy victory.
</p><p><a href="/index.php/Boarding_pods" title="Boarding pods">Boarding pods</a> are a "missile" which delivers marines from the firing ship to the target.
</p><p>Attack missiles have a variety of properties.<br />
<b>Damage yield</b> is the amount of damage inflicted on targets within the <b>Area of Effect</b>.<br />
<b>Range</b> describes how far the missile can travel, generally you want to launch at targets well within range to compensate for target movement.<br />
<b>Speed</b> describes how quickly it moves towards a target.<br />
<b>Cooldown</b> describes how much time the launcher takes between firings to prepare the next launch.<br />
<b>Seeker</b> determines how the missile acquires targets and when it may be fired: <b>Targetted</b>, requiring a target prior to launch or <b>Autonomous</b> which relies on the missile's onboard systems.<br />
<b>Swarming</b> refers simply to missiles which release multiple warheads. There are always eight <b>warheads</b> released. Note: warhead behavior is detailed in other columns such as dumbfire, heat-seeking, etc.<br />
<b>Guidance</b> determines how a missile tracks and follows targets: <b>Heat seeking</b> missiles will attempt to attack the nearest warm target, <b>Image Recognition</b> or <b>Standard Guidance</b> will follow a specific target primarily based on its appearance, <b>Dumb-fire</b> have no guidance at all and will travel in direction of launch without maneuvering relying on the pilot's intuition of target movement.<br />
<b>Fusing</b> controls detonation, most missiles detonate when they <b>impact</b> a target. <b>Proximity fusing</b> will detonate when the missiles gets close enough to the target, generally once it's already missed impact and is getting farther away. A few missiles may be <b>Manually Triggered</b> to detonate remotely, also known as <b>synaptic triggering</b>.<br />
Some missiles have extremely powerful detonations which affect large areas the size of this influence is noted by <b>Area of Effect</b>.<br />
</p>
