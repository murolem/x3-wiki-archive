<h2><span class="mw-headline" id="Description">Description</span></h2>
<p>Weaver's Tempest is the <a href="/index.php/Yaki" title="Yaki">Yaki</a>'s Core sector, though their Shipyard is located to the north in <a href="/index.php/Senator%27s_Badlands" title="Senator's Badlands">Senator's Badlands</a>. The sector is patrolled by the Yaki flagship, the <a href="/index.php/Shuri" title="Shuri">Shuri</a>.
</p>
<h3><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span></h3>
<p>Home to the Yaki, the most deadly and ruthless of pirates, Kailan Weaver founded this sector after the largest pirate offensive ever known against <a href="/index.php/Split" title="Split">Split</a> forces attempting to gain a permanent foothold. Her stunning victory, with calculated strikes that bore the mark of military precision, won her the honour and respect of the Split, and a sector now dedicated to her memory.
</p>
