<p>A free standing <a href="/index.php/Missions#Offers" title="Missions">Offer</a> in space.
</p><p>Buy Blueprints for you HQ to save time reverse engineering them. Keep an eye out for rare ships that have their blueprints for sale. HQ recquired. - by tidurlagi
</p><p>The ships being offered depend on the race owning the sector.  You can be offered M5-M3 and M6 blueprints.  I have never seen M1, M2 or M6+ blueprints.  Go to Yaki sectors to buy Yaki blueprints and to pirate sectors to buy pirate blueprints (up to pirate osprey and pirate centaur).  Note that this way you can get access to ships that can not be bought, for example you can get a Yaki Rajin Raider blueprint, while the Yaki shipyard only sells regular Rajin.
</p>
