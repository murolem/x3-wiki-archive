==Description==
This strange system is named for a [[goner]] belief that Nathan R Gunne once visited this system, the reason for this belief is that when first entered, a piece of debirs was found, after an investigation it was foundyo be from two ancient fleets. All the debris has been cleared up and a new system has been forged in the brown nitrogen dioxide nebula streams of the system. Pirate activity is also increased and has meant increased military patrols in this system. many pilots tend to avoid the gas clouds, as pirate ships have been known to hide in the nebulae and then participate in surprise attacks. The local population are made of mixed labour level, from high tech support to miners.

