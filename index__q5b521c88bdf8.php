<h2><span class="mw-headline" id="Overview">Overview</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Spaceflies&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<h3><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Spaceflies&amp;action=edit&amp;section=2" title="Edit section: Encyclopedia Entry">edit</a><span class="mw-editsection-bracket">]</span></span></h3>
<p>This creature is a small unintelligent space fly that lives in the vast emptiness of deep space. They are the size of a small bird and seem to be made out of delicate shining gossamer that reacts to light and shimmers and shines in a multitude of colours.  The Markus live in vast complex hives hollowed out of asteroids and perform a valuable function by collecting space junk and other debris, which they capture and take back to their asteroids to be used in the construction of their hives. They travel together in great swarms and communicate with a series of musical notes, similar to bird song, that echo inside space. Their beauty, and the beauty of their music, is famed throughout the X Universe and all intelligent space races delight in seeing them. Pilots will stop to watch a swarm fly past. Creatures working on the outside of ships and space stations have claimed the Markus will fly near them and even perch on them, singing all the while. However throughout space sightings of The Markus Space Fly are becoming very rare. This is mainly due to the actions of The Split, who capture and use the small space fly to power their spaceships. Currently the Foundation Guild are pressing for the Markus to be made a protected species and pressure is being put on the Split to change their space ship engines. It is believed that, despite a universe ban on their capture or trading, some pirates still hunt and capture this creature in order to sell them to the Split
</p>
