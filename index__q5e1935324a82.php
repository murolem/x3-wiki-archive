<h2><span class="editsection">[<a href="/index.php?title=Combat&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a>]</span> <span class="mw-headline" id="Overview">Overview</span></h2>
<p>Combat can be defined as the physical expression of hostile intention. 
</p><p>Combat encompasses any situation where offensive action is taken by one ship against another with the intent to damage or destroy. The area which this occurs in is known as the 'combat zone' or 'battlespace'. Operation near an active battlespace is treacherous for any vessel, irrespective of that vessel's intention to get involved in the battle.
</p><p>There are places where battlespaces can be found more frequently, such as on the edges of Pirate and Xenon space, as well as on the borders of warring nations; Split/Boron borders as well as Argon/Paranid borders. Some pilots seek them out, for fun, for bounty, for salvage or for the rewards of pirating ships.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Combat&amp;action=edit&amp;section=2" title="Edit section: Rating">edit</a>]</span> <span class="mw-headline" id="Rating">Rating</span></h2>
<p>Emerging victorious from combat increases the victor's <a href="/index.php/Ranks#Combat_ranks" title="Ranks">combat ranking</a> and marks them as a more dangerous foe.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Combat&amp;action=edit&amp;section=3" title="Edit section: Combat strategies">edit</a>]</span> <span class="mw-headline" id="Combat_strategies">Combat strategies</span></h2>
<p>This section details common strategies for an offensive player ship (on the left) against an opponent (on the right).
</p><p>Typically combat devolves into maneuvering into an opponents blind or weak spot and systematically pounding through shield and hull.
</p>
<h3><span class="editsection">[<a href="/index.php?title=Combat&amp;action=edit&amp;section=4" title="Edit section: Fighter-Fighter">edit</a>]</span> <span class="mw-headline" id="Fighter-Fighter">Fighter-Fighter</span></h3>
<p>Fighters are maneuverable and respond quickly to control inputs. Most fighter engagements revolve around high speed passes which progressively erode light shields and armor. Most fighters do not have rear turrets which gives a trailing fighter an advantage.
</p><p>Heavy fighters which have rear turrets somewhat upset standard fighter dogfights as they can constantly have an enemy under fire, also the turret makes them very resistant to missile fire. On the other hand rear turrets typically don't have the firepower to seriously damage a vigilant foe and act primarily as a deterrent.
</p>
<h3><span class="editsection">[<a href="/index.php?title=Combat&amp;action=edit&amp;section=5" title="Edit section: Fighter-Corvette">edit</a>]</span> <span class="mw-headline" id="Fighter-Corvette">Fighter-Corvette</span></h3>
<p>While quick, corvettes are at a distinct disadvantage in maneuverability compared to fighters. The primary difficulty for an attacking fighter is avoiding the turret weapons long enough to kill the corvette before your own shields evaporate.
</p>
<h3><span class="editsection">[<a href="/index.php?title=Combat&amp;action=edit&amp;section=6" title="Edit section: Fighter-Capital">edit</a>]</span> <span class="mw-headline" id="Fighter-Capital">Fighter-Capital</span></h3>
<p>Capital ships typically sport massive arrays of weapons. However, their turrets are usually packed with anti-capital ship weapons. The deadly exceptions are often the top and bottom turrets which sport vicious area effect flack cannons. It is usually recommended that fighters study capital ship designs to seek out blind spots in their flack coverage, or better yet avoid them completely and attempt saturation bombardment safely outside of flack range.
</p><p>Getting caught by flack is usually difficult to escape from, as often times the first warning is the ship rumbling, at which point the shields fail and the hull starts taking damage. Hull damage reduces speed and makes you an easy target. Light fighters will often just explode if caught by flack, so be wary.
</p>
<h3><span class="editsection">[<a href="/index.php?title=Combat&amp;action=edit&amp;section=7" title="Edit section: Corvette-Fighter">edit</a>]</span> <span class="mw-headline" id="Corvette-Fighter">Corvette-Fighter</span></h3>
<p>Corvettes are sluggish and less maneuverable than fighters, but usually sport several turrets which gives them much better weapons coverage. You can't out run or out maneuver your foes, but you'll last much longer and it only takes a few mis-steps by a fighter before you can finish them off.
</p>
<h3><span class="editsection">[<a href="/index.php?title=Combat&amp;action=edit&amp;section=8" title="Edit section: Corvette-Corvette">edit</a>]</span> <span class="mw-headline" id="Corvette-Corvette">Corvette-Corvette</span></h3>
<h3><span class="editsection">[<a href="/index.php?title=Combat&amp;action=edit&amp;section=9" title="Edit section: Corvette-Capital">edit</a>]</span> <span class="mw-headline" id="Corvette-Capital">Corvette-Capital</span></h3>
<p>Corvettes are at horrible disadvantages vs. capital ships. Corvettes are fast, but not as fast as a fighter. They're tough, but not enough to take many hits from a capital ship weapon. However, corvettes are just small and fast enough to avoid capital ship primaries and heavy enough to mount weapons that can hurt a capital ship, for example:
</p><p>To destroy any ship you just need one <a href="/index.php/M6" title="M6">M6</a> or <a href="/index.php/M6%2B" title="M6+">M6+</a> with some long range weapon (for example <a href="/index.php/Skiron" title="Skiron">Skiron</a> with <a href="/index.php/Concussion_Impulse_Generator" title="Concussion Impulse Generator">Concussion Impulse Generator</a>) and <a href="/index.php/Jumpdrive" title="Jumpdrive">Jumpdrive</a> and then use my special strategy:
</p>
<ol><li>You should fly from gate to enemy ship.
</li><li>Then if you are near to this ship (about 6km - depends on approach speed and weapon ragnge) begin jumping to gate behind.
</li><li>About 5s later when is this ship in your weapon range you should use your weapons and use all of your laser capacity.
</li><li>10s after 2. ship will jump to gate and your laser capacity will be refilled. 
</li><li>Do it again until you destroy enemy ship.
</li></ol>
<p>PS: It is better to use gate near the enemy and if you think that enemy ship is too close to your gate you can use another gate.
</p>
<h3><span class="editsection">[<a href="/index.php?title=Combat&amp;action=edit&amp;section=10" title="Edit section: Capital-Fighter">edit</a>]</span> <span class="mw-headline" id="Capital-Fighter">Capital-Fighter</span></h3>
<h3><span class="editsection">[<a href="/index.php?title=Combat&amp;action=edit&amp;section=11" title="Edit section: Capital-Corvette">edit</a>]</span> <span class="mw-headline" id="Capital-Corvette">Capital-Corvette</span></h3>
<p>Vaporise it.
</p>
<h3><span class="editsection">[<a href="/index.php?title=Combat&amp;action=edit&amp;section=12" title="Edit section: Capital-Capital">edit</a>]</span> <span class="mw-headline" id="Capital-Capital">Capital-Capital</span></h3>
<h3><span class="editsection">[<a href="/index.php?title=Combat&amp;action=edit&amp;section=13" title="Edit section: Bomber-Capital">edit</a>]</span> <span class="mw-headline" id="Bomber-Capital">Bomber-Capital</span></h3>
<p>Using bombers against capitals is generally a good tactic. However, of all the anti-capital tactics this one takes the most planning and situational awareness of where the target is. The three golden rules for capital atomization are:
</p>
<ol><li>Get a suitable number of bombers and escort fighters for the raid. A good template is 8 bombers and 16 escorts. Outfitting them may be very expensive but at least they wont vanish after fighting the capitals. Afterwards they can be kept later for against capital incursions into your space or if you're really desperate for cash, sold;
</li><li>DO NOT SKIMP ON JUMP DRIVES AND ENERGY CELLS. Having a jump capability in case of emergencies could mean the difference between getting away without a loss, or losing the entire wing. It's generally a good idea to research the amount of energy cells needed to get to a friendly sector;
</li><li>Once you've made allowance for energy consumption load your bombers cargo bays with Tomahawks(Phantoms if using Terran) and their rear turrets with Particle Accelerator Cannons(Electro-Magnetic Plasma Cannons if using Terran) to destroy long range missiles.
</li></ol>
<p>The Engagement:
</p><p>If possible have M5s check where the enemy capital's escorts are (if they have any,) to make sure you don't run slap bang into a group of M3s. After having a quick look at the escorts positions have your M8s form into a wing (quite important to be able to control the entire group at once if requiring to jump). 
</p><p>Then (it's best to personally lead your bombers at this stage, you can always transfer to your ship of choice later once the capital ships are done) with the rest of your bombers in a suitable formation that allows for a free field of fire forwards lead your M8s towards the capital.Once you get within firing distance order all bombers within the wing to launch their Torpedoes. Then turn around, fast (It's best to wait a few seconds before jumping out as the NPC bombers require much more time to fire off their missiles than you do).
</p>
