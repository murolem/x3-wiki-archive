<h2><span class="editsection">[<a href="/index.php?title=Soyery&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a>]</span> <span class="mw-headline" id="Overview">Overview</span></h2>
<p>This factory creates the Paranid foodstuff <a href="/index.php/Soja_Husk" title="Soja Husk">Soja Husk</a> from <a href="/index.php/Soja_Beans" title="Soja Beans">Soja Beans</a> and <a href="/index.php/Energy_Cells" title="Energy Cells">Energy Cells</a>.
</p><p>NPC factories also accept <a href="/index.php/Nostrop_Oil" title="Nostrop Oil">Nostrop Oil</a> as a secondary resource.
</p>
<h3><span class="editsection">[<a href="/index.php?title=Soyery&amp;action=edit&amp;section=2" title="Edit section: Encyclopedia Entry">edit</a>]</span> <span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span></h3>
<p>A Soyery is a Paranid food factory where Soja Beans are processed and converted into Soja husk, a vegetable based foodstuff that is traded throughout the universe. The Paranid also add Stott Spices to the Soja husk to make a number of different varieties and tastes. In addition to Soja Beans and Boron Spices each factory also requires large amounts of Energy and Nostrop.
</p>
