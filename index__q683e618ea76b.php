<h2> <span class="mw-headline" id="Illegal_Wares">Illegal Wares</span></h2>
<p>Depending on whose space you're in, different wares are illegal and will cause authorities to attempt to confiscate them. Refusal to eject the illegal ware will result in weapons fire.
</p><p>In the case of a player factory producing illegal wares, police and military vessels will fire upon the offending station and generally become flagged as hostile.
</p><p><a href="/index.php/Illegal_Wares_by_Faction" title="Illegal Wares by Faction">This page</a> holds the complete list of illegal wares sorted by faction.
