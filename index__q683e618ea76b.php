<h4> <span class="mw-headline" id="Police_Licenses">Police Licenses</span></h4>
<p>The <a href="/index.php/Commonwealth" title="Commonwealth">Commonwealth</a> governments offer <a href="/index.php/Police_license" title="Police license">police licenses</a>, which award bounties against faction enemies.  Factions with whom the player is so licensed are noted with an [L] marking next to their <a href="/index.php/Reputation" title="Reputation">notoriety</a> listing in the "Pilot Status" dialog. Additionally, you may scan ships in that races sector legally. (It’s not considered an aggressive act. This will turn the ships to enemy red if you find illegal goods aboard which can be destroyed with no reputation loss.)
</p><p>For reasons unknown, the <a href="/index.php/Terran" title="Terran">Terrans</a> do not offer a similar license.
</p>
