<div id="mw-content-text" lang="en" dir="ltr" class="mw-content-ltr"><p>Wares are the raw and manufactured goods of the X universe.
</p><p>Everything in space is a ware and generally occupies space.<br />
There are two properties of cargo:
</p>
<ul><li> <a href="/index.php/Cargo_Class" title="Cargo Class">Cargo Class</a>: which describes general dimensions limiting carriage on certain transports. 
</li><li> Volume: a numerical description of space limiting carriage to vessels which have enough total free space.
</li></ul>
