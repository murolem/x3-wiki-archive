<h2><span class="mw-headline" id="Overview">Overview</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Majaglit&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>Majaglit is made from the shell of the Maja Snail and is a very precious material used by many races for creating jewelry, And ornamentation. Due to its precious nature it fetches high prices. The few space jewelries capable of processing Majaglit are famous for high quality artworks.
</p>
<h2><span class="mw-headline" id="Commentary">Commentary</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Majaglit&amp;action=edit&amp;section=2" title="Edit section: Commentary">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>This product is mainly used as a primary resource in the production of <a href="/index.php/Navigation_Relay_Satellite" title="Navigation Relay Satellite">Navigation Relay Satellites and Advanced Satellites</a>.
</p>
