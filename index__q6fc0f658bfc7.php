<h2><span class="mw-headline" id="Claiming">Claiming</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Piracy&amp;action=edit&amp;section=1" title="Edit section: Claiming">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>Pilots of <a href="/index.php/Category:Fighters" title="Category:Fighters">fighters</a> and other small ships will sometimes <a href="/index.php/Bail" title="Bail">bail</a> out when fired on; then, the ship may be <a href="/index.php/Claim" title="Claim">claimed</a> by the player.
</p>
<h2><span class="mw-headline" id="Boarding">Boarding</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Piracy&amp;action=edit&amp;section=2" title="Edit section: Boarding">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p><a href="/index.php/Category:Capital_Ships" title="Category:Capital Ships">Capital ships</a>, <a href="/index.php/M6" title="M6">corvettes</a> and <a href="/index.php/TM" title="TM">TM</a>-class vessels must be <a href="/index.php/Boarding" title="Boarding">boarded</a> with <a href="/index.php/Marines" title="Marines">Marines</a> to be captured.
</p>
<h2><span class="mw-headline" id="Uses">Uses</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Piracy&amp;action=edit&amp;section=3" title="Edit section: Uses">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>There are several reasons for a player to engage in piracy:
</p>
<ul><li> Some of the most powerful or unique ships in their class are not sold or rewarded to the player, and thus can only be obtained via capture. Popular targets include:
<ul><li> Pirate <a href="/index.php/Kestrel" title="Kestrel">Kestrel</a> (M5)</li>
<li> Paranid <a href="/index.php/Hyperion_Vanguard" title="Hyperion Vanguard" class="mw-redirect">Hyperion Vanguard</a> (M6+)</li>
<li> <a href="/index.php/I" title="I">Xenon I</a> (M2+)</li>
<li> ATF <a href="/index.php/Valhalla" title="Valhalla">Valhalla</a> (M2+) </li>
<li> Goner <a href="/index.php/Aran" title="Aran">Aran</a> (M1)</li>
<li> Terran <a href="/index.php/Atmospheric_Lifter" title="Atmospheric Lifter">Atmospheric Lifter</a> (TL)</li></ul></li>
<li> Capturing a ship leaves the cargo hold intact (for most item types). Some rare items, such as the <a href="/index.php/Incendiary_Bomb_Launcher" title="Incendiary Bomb Launcher">Incendiary Bomb Launcher</a> or the <a href="/index.php/Jump_Beacon" title="Jump Beacon">Jump Beacon</a>, can be easier to obtain via capturing, rather than destroying the carrying ship and hoping the cargo survives.</li>
<li> Captured ships, especially larger ones, can sell for a large amount of credits.</li>
<li> Some <a href="/index.php/Missions" title="Missions">Plots</a> involve capturing ships.</li></ul>
