<h2><span class="mw-headline" id="Description">Description</span></h2>
<p>Senator's Badlands is home to the <a href="/index.php/Yaki" title="Yaki">Yaki</a>'s only Shipyard. It is guarded by a large number of fighters, and is usually patrolled by an <a href="/index.php/Akuma" title="Akuma">Akuma</a>.
</p>
<h3><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span></h3>
<p>Mired in the torment of a war without honor, rumor suggests the Yaki have chosen this sector as a point of respite and for many of them, a place to call home. <a href="/index.php/Argon" title="Argon">Argon</a> and <a href="/index.php/Paranid" title="Paranid">Paranid</a> scouts returning from missions have reported ships known to be of Yaki origin, patrolling both this and the adjacent sector of Ocracoke's Storm. Clearly both sectors offer a battlefront upon which to launch attacks, while protecting the dark secrets thought to dwell deep within the Badlands. The sector's name is a clear sign of defiance to the Argon and a warning of past mistakes for the <a href="/index.php/Teladi" title="Teladi">Teladi</a>.
</p>
