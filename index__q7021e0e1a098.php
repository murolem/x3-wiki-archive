<h2><span class="mw-headline" id="Weapon_Types">Weapon Types</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Weapons&amp;action=edit&amp;section=1" title="Edit section: Weapon Types">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<h3><span class="mw-headline" id="Bolt_weapons">Bolt weapons</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Weapons&amp;action=edit&amp;section=2" title="Edit section: Bolt weapons">edit</a><span class="mw-editsection-bracket">]</span></span></h3>
<ul><li>Fire discrete projectiles which travel through space at the listed speed.</li>
<li>May fire repeatedly at the specified rate of fire.</li>
<li>Accuracy depends on the bullet speed, which ranges from slow capital ship cannons to Flak that exceeds the speed of beam weapons.</li>
<li>Most weapons fit into this category.</li></ul>
<h3><span class="mw-headline" id="Beam_weapons">Beam weapons</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Weapons&amp;action=edit&amp;section=3" title="Edit section: Beam weapons">edit</a><span class="mw-editsection-bracket">]</span></span></h3>
<ul><li>Are virtually impossible to evade because all beam weapons have a very high propagation speed.</li>
<li>Generate a beam until the trigger is released or energy is depleted.</li>
<li>Few Beam Weapons commonly seen in-game: The <a href="/index.php/Alpha_Kyon_Emitter" title="Alpha Kyon Emitter">Alpha</a>, <a href="/index.php/Beta_Kyon_Emitter" title="Beta Kyon Emitter">Beta</a> and <a href="/index.php/Gamma_Kyon_Emitter" title="Gamma Kyon Emitter">Gamma</a> Kyon Emitters, the <a href="/index.php/Pulsed_Beam_Emitter" title="Pulsed Beam Emitter">Pulsed Beam Emitter</a> and the <a href="/index.php/Ion_Disruptor" title="Ion Disruptor">Ion Disruptor</a>.</li></ul>
<h3><span class="mw-headline" id="Shockwave_weapons">Shockwave weapons</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Weapons&amp;action=edit&amp;section=4" title="Edit section: Shockwave weapons">edit</a><span class="mw-editsection-bracket">]</span></span></h3>
<ul><li>Are almost impossible to evade since some can cover an area that is over 1 km wide.</li>
<li>Fire a cone of destruction hitting victims multiple times, inflicting a huge multiplier on large targets.</li>
<li>Often a source of friendly fire.</li>
<li>Few Shockwave weapons available: Only the <a href="/index.php/Phased_Shockwave_Generator" title="Phased Shockwave Generator">Phased Shockwave Generator</a> and the <a href="/index.php/Plasma_Burst_Generator" title="Plasma Burst Generator">Plasma Burst Generator</a> are available.</li></ul>
<h3><span class="mw-headline" id="Flak_weapons">Flak weapons</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Weapons&amp;action=edit&amp;section=5" title="Edit section: Flak weapons">edit</a><span class="mw-editsection-bracket">]</span></span></h3>
<ul><li>Are primarily designed as anti-fighter weapons.</li>
<li>Create a large damage area capable of seriously damaging fighters.</li>
<li>Very few Flak Weapons are available: The <a href="/index.php/Fragmentation_Bomb_Launcher" title="Fragmentation Bomb Launcher">Fragmentation Bomb Launcher</a>, <a href="/index.php/Flak_Artillery_Array" title="Flak Artillery Array">Flak Artillery Array</a>, <a href="/index.php/Starburst_Shockwave_Cannon" title="Starburst Shockwave Cannon">Starburst Shockwave Cannon</a> and <a href="/index.php/Cluster_Flak_Array" title="Cluster Flak Array">Cluster Flak Array</a></li></ul>
<h3><span class="mw-headline" id="Ammunition_Based_Weapons">Ammunition Based Weapons</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Weapons&amp;action=edit&amp;section=6" title="Edit section: Ammunition Based Weapons">edit</a><span class="mw-editsection-bracket">]</span></span></h3>
<ul><li>Far more energy efficient than their equivalents, they require constant supplies of ammunition to fire.</li>
<li>Each ammunition crate holds 200 rounds of ammunition. This means certain weapons use ammunition faster than others.</li>
<li>Storing and re-equipping becomes a matter of logistics. <a href="/index.php/OOS" title="OOS" class="mw-redirect">OOS</a> combat uses ammunition very quickly.</li>
<li>Few Ammunition Based Weapons Available: The <a href="/index.php/Mass_Driver" title="Mass Driver">Mass Driver</a>, <a href="/index.php/Energy_Bolt_Chaingun" title="Energy Bolt Chaingun">Energy Bolt Chaingun</a>, <a href="/index.php/Matter/Anti-Matter_Launcher" title="Matter/Anti-Matter Launcher">Matter/Anti-Matter Launcher</a> (and  <a href="/index.php/Prototype_Matter/Anti-Matter_Launcher" title="Prototype Matter/Anti-Matter Launcher">P-M/AML</a>) and the <a href="/index.php/Gauss_Cannon" title="Gauss Cannon">Gauss Cannon</a> are the only weapons requiring Ammunition.</li>
<li>Available Ammunition Types: <a href="/index.php/Mass_Driver_Ammunition" title="Mass Driver Ammunition">Mass Driver Ammunition</a>, <a href="/index.php/Energy_Bolt_Chaingun_Ammunition" title="Energy Bolt Chaingun Ammunition">Energy Bolt Chaingun Ammunition</a>, <a href="/index.php/Gauss_Cannon_Ammunition" title="Gauss Cannon Ammunition">Gauss Cannon Ammunition</a> and <a href="/index.php/Matter/Anti-Matter_Warhead" title="Matter/Anti-Matter Warhead"> Matter/Anti-Matter Warheads</a></li></ul>
<h2><span class="mw-headline" id="Damage">Damage</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Weapons&amp;action=edit&amp;section=7" title="Edit section: Damage">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>Damage is inflicted in two ways.
</p>
<h4><span class="mw-headline" id="Hull">Hull</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Weapons&amp;action=edit&amp;section=8" title="Edit section: Hull">edit</a><span class="mw-editsection-bracket">]</span></span></h4>
<p>Hulls are the outer shells of ships, they usually protect the equipment that makes a ship a ship rather than a blasted out derelict.
Damage to the hull directly damages the ship and its equipment.
</p>
<h4><span class="mw-headline" id="Shield">Shield</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Weapons&amp;action=edit&amp;section=9" title="Edit section: Shield">edit</a><span class="mw-editsection-bracket">]</span></span></h4>
<p><a href="/index.php/Shields" title="Shields">Shields</a> are a protective energy field which primarily keeps micrometeorites and other debris from harming the hull in the course of normal navigation. They also protect the hull from weapons fire. Typically the shields must first be overcome to damage a ship.
</p>
