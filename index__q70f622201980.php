==Overview==
===Encyclopedia Entry===
The Explorer software was designed by Teladi deep space researchers to enable remote entry and exploration of potentially hostile sectors without risking a pilot's life. Manned research vessels usually follow after the sector has proved to be safe.

===Available Commands===
&lt;onlyinclude>
====Explorer Command Software====
*Deploy Satellite Network
*Fly Through Gate...
*Map Sector...
*Scan Asteroids
&lt;/onlyinclude>

==Commentary==
Equipping a fast ship, such as the Kestrel, will allow you to quickly and autonomously map a sector and scan all its asteroids.

