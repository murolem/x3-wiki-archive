<h2><span class="editsection">[<a href="/index.php?title=Impulse_Ray_Emitter&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a>]</span> <span class="mw-headline" id="Overview">Overview</span></h2>
<p>The Impulse Ray Emitter is the lightest weapon available, commonly fitted to M5 class ships.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Impulse_Ray_Emitter&amp;action=edit&amp;section=2" title="Edit section: Encyclopedia Entry">edit</a>]</span> <span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span></h2>
<p>This is a basic laser that is used in many light weapons and smaller fighter craft.
</p><p>It has a extremely high rate of fire, is very light, and is an excellent weapon for missile defense purposes in turrets.
</p><p>The Impulse Ray Emitter is one of the oldest energy-based weapons around. They require not much power to fire, and usually have quite a rapid fire rate. No one actually knows when the weapon was first developed.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Impulse_Ray_Emitter&amp;action=edit&amp;section=3" title="Edit section: Commentary">edit</a>]</span> <span class="mw-headline" id="Commentary">Commentary</span></h2>
<p>The IRE is a <a href="/index.php/M5" title="M5">Scout</a> class weapon that simply isn't powerful enough to deal appreciable damage to anything bigger. They're usually only seriously used for missile defence, for which their good accuracy excels, their poor <a href="/index.php/Damage_per_second" title="Damage per second">DPS</a> is an irrelevance and their minute power consumption an advantage if heavier weapons are draining the batteries.
</p>
