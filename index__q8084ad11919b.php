<h2><span class="mw-headline" id="Overview">Overview</span></h2>
<p>The High Energy Plasma Thrower, or HEPT, is usually considered a heavy fighter or medium corvette weapon.
</p>
<h2><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span></h2>
<p>A weapon found in many medium to larger fighter craft. The radiation is so dangerous that the weapon had to go through many years of research and development until it could be placed on the market. This was worked around by adding a radiation sink into the weapon's main design. Purging the weapon of all excess radiation between shots.
</p>
<h2><span class="mw-headline" id="Commentary">Commentary</span></h2>
<p>The HEPT is one of the oldest weapons around in the <a href="/index.php/Commonwealth" title="Commonwealth">Commonwealth</a> and once upon a time was the staple of capital ships. Nowadays however, it has been surpassed by newer weapons in many areas, for example as a light weapon by the <a href="/index.php/Phased_Repeater_Gun" title="Phased Repeater Gun">Phased Repeater Gun</a> which has very similar characteristics except for far greater accuracy than the HEPT. As a legacy weapon, it is now a weapon equipped primarily by <a href="/index.php/M6" title="M6">M6</a> and smaller ships needing to deal with heavy fighters, corvettes and frigates. However, it is effective against just about everything, although it is a struggle to hit faster M5s with it and it requires numbers to bring down anything larger than a frigate. It is the staple weapon of many M3 class ships and can frequently be found in both main guns and turrets of corvettes, being significantly more energy efficient than the corvette-level <a href="/index.php/CIG" title="CIG" class="mw-redirect">CIG</a>, <a href="/index.php/ISR" title="ISR" class="mw-redirect">ISR</a> or <a href="/index.php/IPG" title="IPG" class="mw-redirect">IPG</a>.
</p><p>Because of its legacy, it is in widespread use and can be found almost anywhere.
</p>
