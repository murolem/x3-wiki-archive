<h2><span class="editsection">[<a href="/index.php?title=Flail_Barrage_Missile&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a>]</span> <span class="mw-headline" id="Overview">Overview</span></h2>
<p>Exclusively mountable on M7M class vessels, Flail Barrage Missiles are a horribly effective anti-fighter <a href="/index.php/Missiles#Overview" title="Missiles">swarm</a> missile that will re-acquire targets once the original is destroyed.
</p>
<h3><span class="editsection">[<a href="/index.php?title=Flail_Barrage_Missile&amp;action=edit&amp;section=2" title="Edit section: Encyclopedia Entry">edit</a>]</span> <span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span></h3>
<p>Early missile frigate prototypes proved vulnerable to fighter attacks. The Flail missile was therefore designed to counter this, taking advantage of both multiple warheads and advanced target-acquisition technology. Being caught in a Flail barrage is extremely deadly for any nearby enemy fighters.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Flail_Barrage_Missile&amp;action=edit&amp;section=3" title="Edit section: Commentary">edit</a>]</span> <span class="mw-headline" id="Commentary">Commentary</span></h2>
<p>Flails are predominantly anti-fighter weapons, but launched in large numbers are effective at taking down capital class shielding; in fact, they are commonly used in <a href="/index.php/Boarding" title="Boarding">boarding</a> operations for fine control of the target's shields whilst the hull is breached by the attacking <a href="/index.php/Marines" title="Marines">Marines</a>.
</p><p>They can be launched by barrage, however it is actually quicker to launch them singly, as their very fast refire rate means that the limiting factor is how fast the player can spam the launch key.
</p>
