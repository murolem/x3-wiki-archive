==Description==
When the Teladi Mining Executive came across this system, they sent several of their Explorer Ships to evaluate the system's resources. One of the ships, the Mercantile, came across abandoned space station set into some of the mineral rich asteroids. The technology seemed to be similar to the [[Argon]], however they denied ever having held a presence there. The neighbouring system already belonged to the Teladi, so they quickly moved in, re-aligning the gates with the standard navigation system, near to the alien mines. After retrofitting the stations with Teladi technology, the largest Teladi mining operation in history opened for business.

