<p>Outwardly a non-governmental security organization, the activities of this outfit suggests that they don't always subscribe to Paranid ideologies and ethics.
</p><p>Duke's Dangerous Materials Transporters are a common sight around the X-Universe and are notorious for being a source of racial <a href="/index.php/Reputation" title="Reputation">reputation</a> and <a href="/index.php/Capping" title="Capping" class="mw-redirect">free ships</a>.
