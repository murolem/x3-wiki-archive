<p><br />
</p><p><br />
The Terrans are humanity and are the most technologically advanced race of the known universe. Although isolated from the other races several centuries, they have now been re-connected to the gate network in recent years.
</p><p>Now that they are once again connected to the gate network, relationships between the Terrans and other races play an important role. Terran space is guarded not only by their military but by the specialist <a href="/index.php/ATF" title="ATF">AGI Task Force</a>. The Terrans are on shaky ground with the <a href="/index.php/Paranid" title="Paranid">Paranid</a>, but many of the other races are trying to build relations, including their estranged cousins, the <a href="/index.php/Argon" title="Argon">Argon</a>.
</p><p>Meanwhile, the Terraformers haunt space, as remnants from the old solar-empire gate network and form the fleet which followed Nathan have rebuilt and multiplied. They are now simply known as the <a href="/index.php/Xenon" title="Xenon">Xenon</a>.
</p>
