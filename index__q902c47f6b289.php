<a name="History" id="History"></a><h2><span class="editsection">[<a href="/index.php?title=Terran&amp;action=edit&amp;section=4" title="Edit section: History">edit</a>]</span> <span class="mw-headline"> History </span></h2>
<p>After finding the jumpgate systems through their own jumpgate experiments and expanding to create a huge empire they suffered a terrible fate; the commands of the Terraformer ships sent out to transform planets into habitable worlds went horribly wrong.
</p><p>When the Terraformers were no longer needed a message was broadcasted to them telling them to shut off. The programming was slightly dodgy however, and sooner or later several groups of these ships decided to terraform everything and anything they could find, including Earth.
</p><p>Earth and it's intra- and extra-solar colonies fell quickly to the war machines which replicated on technology they'd taken from the Terrans, becoming more and more intelligent.
</p><p>Eventually, Nathan R. Gunne flies his ship through the Earth, leading the terraformer fleet through, then jumpgate blowing it behind them. The descendants of the ship's crew rebuild a society and in time come to be as large as any of the others races, named the '<a href="/index.php/Argon" title="Argon">Argon</a>' in memory of Nathan R. Gunne. The Terrans were saved, barely, and over the next 600-700 years rebuilt their solar empire, until eventually another gate was connected, linking them to the commonwealth.
</p>
