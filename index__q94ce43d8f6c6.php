<h2><span class="mw-headline" id="Description">Description</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Family_Njy&amp;action=edit&amp;section=1" title="Edit section: Description">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>This agricultural system is run by the brutally efficient Njy family. It produces many Split foodstuffs that are shipped to the two planets in this system and neighbouring systems. The Njy family is particularly brutal, offensive and arrogant, even by Split standards.
</p>
