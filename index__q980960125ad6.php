<h2><span class="mw-headline" id="Description">Description</span></h2>
<p>Located near the giant star of Lehril, this system is a source of strategic power for the Boron. Being a border colony to the local Split Family, this sector also acts as a crossroads for other Boron settlements, making it particularly important for passing trade. It is guarded by a lone destroyer with escorts, thus pirate activity here is minimal.
</p>
