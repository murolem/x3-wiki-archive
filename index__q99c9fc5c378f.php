<h2> <span class="mw-headline" id="Overview">Overview</span></h2>
<p>Boarding is the act of getting <a href="/index.php/Marines" title="Marines">marines</a> onto a ship you don't control.
</p><p>The marines then progress through a number of decks to get to the controls and hack them.
</p><p>If successful, the boarding operation grants control of the ship to the boarding faction.
</p><p>Only a few classes of ship are <a href="/index.php/Category:Boarding_Ship" title="Category:Boarding Ship">Boarding Ships</a>.
</p><p>Only a few <a href="/index.php/Category:Board_to_Capture" title="Category:Board to Capture">Classes of Ship</a> need to be boarded to be <a href="/index.php/Capping" title="Capping">captured</a>.<br />
Others like <a href="/index.php/Category:Fighters" title="Category:Fighters">Fighters</a> may be forced to <a href="/index.php/Bail" title="Bail">bail</a> and then be <a href="/index.php/Claim" title="Claim">claimed</a>
</p>
<h2> <span class="mw-headline" id="Types_of_Boarding">Types of Boarding</span></h2>
<p>There are two types of boarding:
</p>
<h4> <span class="mw-headline" id="Spacewalk"> Spacewalk</span></h4>
<p>Marines are put directly in space and must use suit units to get in contact with the hull.
</p><p>Each marine must make transit as an individual and cut through the hull by themselves.  Scattering from launch or landing may separate a boarding team, which will reduce their effectiveness as they take casualties from their isolated combat power facing the total defensive might of the ship.
</p>
<h4> <span class="mw-headline" id="Boarding_Pods"> Boarding Pods</span></h4>
<p><a href="/index.php/Boarding_pods" title="Boarding pods">Pods</a> containing a marine boarding team of up to 5 marines are launched from a missile frigate and upon impacting the hull begin to cut through.
</p><p>Pods may be destroyed in transit so it is worthwhile to suppress anti-missile defenses during transit.
</p>
<h2> <span class="mw-headline" id="Steps_To_Board">Steps To Board</span></h2>
<ol><li> Load marines onto a boarding ship.
</li><li> Give a 'Piracy' order to board a valid target.
</li><li> Keep the target's shields down until the marines are inside the hull.
</li><li> Monitor marine chatter and wait until they've successfully hacked the firewall.
</li></ol>
<p>The player may also directly control the boarding operation, in which case the player will launch the marines manually.<br />
Use "launch marines" from "additional ship commands" under the turret control options.
</p>
<h2> <span class="mw-headline" id="Other_Considerations">Other Considerations</span></h2>
<ul><li> Boarding causes hull damage, so a heavily damaged ship may explode or otherwise be impossible to board successfully.
</li><li> A ship changing allegiance in the midst of enemies will be fired upon by those enemies.
</li><li> Like most captured ships, boarded ships often lose all their shields.
</li><li> Different classes of ships may take on different <a href="/index.php/Marines#Aboard_Ship" title="Marines">numbers of marines</a>.
</li><li> Ships have a base resistance level and may have devices or marines on board to make success more difficult.
</li><li> Marine combat skill can be improved by boarding applicable ships in <a href="/index.php/Return_Ship" title="Return Ship">Return Ship</a> missions.
</li><li> Marine combat skills only improve through actual boarding operations.
</li></ul>
<h2> <span class="mw-headline" id="Valid_Target_Classes">Valid Target Classes</span></h2>
<p>Usually, only large vessels can be boarded.
</p><p>These ships can be found listed <a href="/index.php/Category:Board_to_Capture" title="Category:Board to Capture">Here</a>.
</p>
<h2> <span class="mw-headline" id="Boarding_Resolution_Algorithm">Boarding Resolution Algorithm</span></h2>
<p>Boarding resolution is evaluated by comparing the relative combat power of the boarders versus the defense power of the ship.
</p><p>Thus a concentrated boarding will be more effective as the boarding team's combat power all comes in together in a large block.
</p><p>A separated team will face the defense of the ship in small groups and likely take casualties while the team reforms aboard ship, this in turn will likely reduce the total power of the team and generate more casualties.
</p>
