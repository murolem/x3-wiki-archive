<p>Established at the same time as '<a href="/index.php/New_Income" title="New Income">New Income</a>', this well organised Teladi system supports most of the ships travelling through newly colonised Teladi space. The inhospitable, rocky moon of the fringes of this system is used as a survival training ground for Teladi Profit-Protection troops.
