<h2><span class="mw-headline" id="Overview">Overview</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Particle_Accelerator_Cannon&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>The Particle Accelerator Cannon, or PAC, is a <a href="/index.php/Commonwealth" title="Commonwealth">Commonwealth</a> medium fighter weapon.
</p>
<h2><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Particle_Accelerator_Cannon&amp;action=edit&amp;section=2" title="Edit section: Encyclopedia Entry">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>A weapon found on some medium to large fighters.
</p><p>The first of its class were developed by the Paranid and Argon. The Particles are accelerated to a speed faster than nearly most currently known spacecraft. When they are then polarized and focused into a tight energy pulse. They have been around since the Xenon conflict.
</p>
<h2><span class="mw-headline" id="Commentary">Commentary</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Particle_Accelerator_Cannon&amp;action=edit&amp;section=3" title="Edit section: Commentary">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>The PAC is a light but very versatile weapon. It has been around for centuries and most ships can equip it. Like the <a href="/index.php/High_Energy_Plasma_Thrower" title="High Energy Plasma Thrower">HEPT</a>, it is a good all rounder, tending towards the lighter targets like <a href="/index.php/M5" title="M5">scouts</a>, <a href="/index.php/M4" title="M4">interceptors</a> and <a href="/index.php/M3" title="M3">heavy fighters</a>. However, fighters equipped with these weapons in numbers have been seen to bring down anything, including the biggest capital ships. It is also the primary weapon of the <a href="/index.php/Fighter_Drone_MKII" title="Fighter Drone MKII">Mark II</a> variant of the <a href="/index.php/Fighter_Drone" title="Fighter Drone">Fighter Drone</a>.
</p><p>The PAC uses comparatively little energy, and its fast shot speed give it very good accuracy. It is also an extremely cheap weapon, which is why it frequently appears as the weapon of choice in large fighter wings.
</p><p>This weapon is ubiquitous (found anywhere).
</p>
