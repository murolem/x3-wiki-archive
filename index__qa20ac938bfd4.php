<h2><span class="editsection">[<a href="/index.php?title=Aurora_Missile&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a>]</span> <span class="mw-headline" id="Overview">Overview</span></h2>
<p>The Aurora is a small, weak and fast <a href="/index.php/Missiles#Overview" title="Missiles">dumbfire</a> missile with synaptic triggering.
</p><p>The Aurora is classed as a Synaptic Detonator missile, a new and revolutionary technology that allows the pilot to choose when to detonate his missile to the split second of pressing the trigger. The pilot first fires his missile and then uses the fire missile trigger again to detonate the missile before the onboard timer expires.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Aurora_Missile&amp;action=edit&amp;section=2" title="Edit section: Commentary">edit</a>]</span> <span class="mw-headline" id="Commentary">Commentary</span></h2>
<p>The Aurora is officially classified as a Manual/Proximity fused missile. However, in practice it is purely a manually detonated weapon with an automatic timer.
</p><p>The Aurora is capable of doing serious damage to light fighters, but being dumbfire, the piloting skill of the user has to be incredible to get the accuracy to land the hit. Furthermore, only one can be launched at a time due to the synaptic detonation triggering the missiles auto-destruct when the trigger is pressed after launch.
</p><p>All in all, dumbfire missiles are not a popular choice of weapon.
</p>
