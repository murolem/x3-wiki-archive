<h2><span class="editsection">[<a href="/index.php?title=Build_Stations&amp;action=edit&amp;section=1" title="Edit section: Objective">edit</a>]</span> <span class="mw-headline" id="Objective">Objective</span></h2>
<p>Buy, transport and build a designated factory in a designated position within a set time limit.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Build_Stations&amp;action=edit&amp;section=2" title="Edit section: General Difficulty:">edit</a>]</span> <span class="mw-headline" id="General_Difficulty:">General Difficulty:</span></h2>
<p>Very Easy to Medium.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Build_Stations&amp;action=edit&amp;section=3" title="Edit section: What you will need:">edit</a>]</span> <span class="mw-headline" id="What_you_will_need:">What you will need: </span></h2>
<ul><li> A <a href="/index.php/TL" title="TL">TL</a>-class ship (hired or owned). If you own it and it is jumpdrive-equipped this mission is a cakewalk. Otherwise, you may find the schedule a bit touch-and-go.
</li><li> An account balance of in excess of several million <a href="/index.php/Cr" title="Cr" class="mw-redirect">Cr</a>.
</li><li> Good rep with the factory's native Faction.
</li></ul>
<h2><span class="editsection">[<a href="/index.php?title=Build_Stations&amp;action=edit&amp;section=4" title="Edit section: What you will find:">edit</a>]</span> <span class="mw-headline" id="What_you_will_find:">What you will find: </span></h2>
<ul><li> A beacon will be dropped, on which the station should be dropped.
</li><li> You will have up to three hours depending on mission difficulty.
</li></ul>
<h2><span class="editsection">[<a href="/index.php?title=Build_Stations&amp;action=edit&amp;section=5" title="Edit section: Commentary and Strategy">edit</a>]</span> <span class="mw-headline" id="Commentary_and_Strategy">Commentary and Strategy</span></h2>
<p>These missions are very straightforward and pay very well, but without your own TL, getting the factory transported and built on time can pose problems. It is advisable to have a TL hired before accepting the mission, if you don't own your own.
</p><p>A big risk is failing to build the factory in the right place; it must be within range of the planted beacon. Using the match position key (numpad 5 by default) on the beacon and placing it directly on top is the best plan; the beacon will disappear, leaving the factory no longer crunching the beacon.
</p><p>Note that after factory placement, the factory will be removed from your control and you will no longer own it. You will then pick up your reward, of course.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Build_Stations&amp;action=edit&amp;section=6" title="Edit section: Extra Revenue (A Teladi way?)">edit</a>]</span> <span class="mw-headline" id="Extra_Revenue_.28A_Teladi_way.3F.29">Extra Revenue (A Teladi way?)</span></h2>
<p>This method increases your turnover from the factory building missions, while in the mission but prior to planting the factory down.
</p>
<ul><li> Check what resources the factory will require once it's operational
</li><li> Make sure you have about one TS per Resource. (These TS's should be kept for Factory missions or Temporarily re-assigned while such missions aren't being conducted)
</li><li> Fit the TS's with jumpdrives (Necessary for delivery speed)
</li><li> Fill each TS with one of the resources the finished factory requires.  (Either use produce from you own factories, or buy bulk loads cheaply from non-player stations)
</li><li> Get your TL or the hired TL to Drop the station (Plant the station and wait for it to change control from you to the AI)
</li><li> Command those TS's to Jump and Dock with the new station and sell all the Resources
</li></ul>
<p>The same method can be used to get your own Factories up and running when you first plant them.
</p>
