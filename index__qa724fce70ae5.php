<h1><span class="mw-headline" id="Territories">Territories</span></h1>
<p>Pirate Sectors tend to be desolate empty places. Only a few scattered, hardy souls are willing enough or tough enough to live in such unregulated places.
</p><p>Pirate Sectors are often littered with asteroids, undisturbed by intensive corporate mining.
</p>
