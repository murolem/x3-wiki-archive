<h1><span class="mw-headline" id="General">General</span></h1>
<p>Pirates are not a race in themselves but groups of like-minded criminals who specialize in all sorts of illegal activities. They have their own bases, usually hidden from view and well defended. Most pirate bases have large fleets of ships and will send out raiding patrols on a regular basis. They are known to be very aggressive and if they spot you across the sector they may start heading your way. If you encounter pirates, you may report them after you have scanned a pirate ship for illegal goods after scanning them with you freight scanner. 
</p><p>Note: You can't see what's in the cargo hold without first scanning the ship and that scanning is illegal if you don't have a police license.
</p>
<h2><span class="mw-headline" id="Government_and_politics">Government and politics</span></h2>
<h3><span class="mw-headline" id="Foreign_relations_and_military">Foreign relations and military</span></h3>
<p>The Pirates are treated as hostile by all governments. As such destroying them will earn you positive reputation while in their sectors. Pirates are often hostile to all ships and will attack them while on patrol. Some Pirate convoys have specific destinations however, and will not attack passing ships.
</p>
<h2><span class="mw-headline" id="History">History</span></h2>
<h2><span class="mw-headline" id="Culture">Culture</span></h2>
<p>Pirate clans are actually separate associations of like-minded criminals who make a living by killing, stealing, smuggling and other unscrupulous activities. Because of the existence of distinct groups, the Pirates are very poorly organised as a whole.
</p>
<h2><span class="mw-headline" id="Geography_and_Climate">Geography and Climate</span></h2>
<h1><span class="mw-headline" id="Science_and_Technology">Science and Technology</span></h1>
<h3><span class="mw-headline" id="Ships">Ships</span></h3>
<p>You will encounter pirate vessels of all types since pirates use other races' ships, modified for their own ends. The wealthy pirate groups even have their own custom ship designs, often including non-standard colours.
</p><p>Do not confuse pirates with smugglers who are much cleverer by using normal ships to conceal their activities.
</p>
