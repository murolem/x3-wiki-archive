<h2><span class="mw-headline" id="Random_Starting_Locations">Random Starting Locations</span></h2>
<ul><li>Tormented Teladi starts in <a href="/index.php/Sanctity_of_Corruption" title="Sanctity of Corruption">Sanctity of Corruption</a>, <a href="/index.php/Tears_of_Greed" title="Tears of Greed">Tears of Greed</a> or <a href="/index.php/Acquisition_Repository" title="Acquisition Repository">Acquisition Repository</a>. </li>
<li>Poisoned Paranid starts in <a href="/index.php/Preacher%27s_Refuge" title="Preacher's Refuge">Preacher's Refuge</a> or <a href="/index.php/Heaven%27s_Assertion" title="Heaven's Assertion">Heaven's Assertion</a>.</li>
<li>Unholy Traitor starts in a sector randomly picked from a limited selection.</li></ul>
<p><br />
</p>
<h2><span class="mw-headline" id="Notes">Notes</span></h2>
<ul><li>Humble Merchant starts with both a Mercury <a href="/index.php/TS" title="TS">TS</a> and a <a href="/index.php/Discoverer" title="Discoverer">Discoverer</a> <a href="/index.php/M5" title="M5">M5</a>.</li>
<li>Aldran Adventurer starts with <a href="/index.php/Terran_plot" title="Terran plot" class="mw-redirect">Terran plot</a> completed, so you cannot get its rewards. </li>
<li>Goner Witness' <a href="/index.php/Ranger" title="Ranger">Ranger</a> cannot equip any weapons or missiles.</li>
<li>Boring Boron starts in hostile territory.</li>
<li>Suicidal Squid uses *Dead is Dead* mode, meaning you cannot reload after dying. It requires a Steam connection.</li>
<li>Custom Game starts with **all plots disabled**. It does not allow you any actual custom choices either.</li></ul>

