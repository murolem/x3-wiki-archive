<h2> <span class="mw-headline" id="Overview">Overview</span></h2>
<p>Capital ships are huge beasts, several kilometers in length, bristling with guns, dominating combat, and are the epitome of destruction. Appropriate to the wild fireworks heralding their arrival in combat, capital ships cost a substantial investments of time and <a href="/index.php/Credits" title="Credits">credits</a>.
</p><p>Capital ships when encountered <a href="/index.php/Out-of-Sector" title="Out-of-Sector" class="mw-redirect">Out-of-Sector</a> carry sufficient firepower to destroy almost <b>all</b> vessels in one turn. As a result un-monitored battles with capital ships will typically devolve to attrition of expendable decoys. Maintaining a patrol squadron in the face of hostile capital ships can be time-consuming and expensive.
</p><p>When encountered <a href="/index.php/In-Sector" title="In-Sector" class="mw-redirect">In-Sector</a>, a hostile capital ship can generate sufficient fire to make approach difficult. If their concentrated firepower is permitted to hit, lesser target vessels will invariably be destroyed or crippled. Fighting between capital ships is typically a slow exchange of fire which will steadily destroy the other ship.
</p><p>Capital Ships can only be <a href="/index.php/Capping" title="Capping">captured</a> by <a href="/index.php/Boarding" title="Boarding">boarding</a> them with <a href="/index.php/Marines" title="Marines">marines</a>.
</p>
