<h2> <span class="mw-headline" id="Docking">Docking</span></h2>
<p>Capital Ships require their own class of docking clamps and hence the range of stations they can dock at is limited.<br />
Freight exchange with other stations must be conducted through intermediaries.
</p><p>A list of docking stations and limits is available <a href="/index.php/Docking#Capital_Ship_Docking_Stations" title="Docking">Here</a>.
</p><p><br />
</p>
<h2> <span class="mw-headline" id="Jumpgates">Jumpgates</span></h2>
<p>When navigating through jumpgates, these ships will use the centre of the gate.
</p>
