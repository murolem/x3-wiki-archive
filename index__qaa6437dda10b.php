<div id="mw-content-text" lang="en" dir="ltr" class="mw-content-ltr"><h2><span class="editsection">[<a href="/index.php?title=Weapons&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a>]</span> <span class="mw-headline" id="Overview">Overview</span></h2>
<p>Weapons are primarily designed to inflict damage on other ships.
</p><p>They may however be used to break up space debris, intercept missiles, and repair ships (with repair lasers).
</p><p>Some weapons are very energy efficient, usually a result of requiring ammunition.
</p><p>Direct fire weapons like those listed below can be characterized as either a beam, area of effect, or a bolt. 
</p>
<h3><span class="editsection">[<a href="/index.php?title=Weapons&amp;action=edit&amp;section=2" title="Edit section: Bolt weapons">edit</a>]</span> <span class="mw-headline" id="Bolt_weapons">Bolt weapons</span></h3>
<ul><li>Fire discrete projectiles which travel through space at the listed speed.
</li><li>May fire repeatedly at the specified rate of fire.
</li><li>Accuracy depends on the bullet speed, which ranges from slow capital ship cannons to Flak that exceeds the speed of beam weapons.
</li></ul>
<h3><span class="editsection">[<a href="/index.php?title=Weapons&amp;action=edit&amp;section=3" title="Edit section: Beam weapons">edit</a>]</span> <span class="mw-headline" id="Beam_weapons">Beam weapons</span></h3>
<ul><li>Are virtually impossible to evade because all beam weapons have a very high propagation speed.
</li><li>Generate a beam until the trigger is released or energy is depleted.
</li></ul>
<h3><span class="editsection">[<a href="/index.php?title=Weapons&amp;action=edit&amp;section=4" title="Edit section: Shockwave weapons">edit</a>]</span> <span class="mw-headline" id="Shockwave_weapons">Shockwave weapons</span></h3>
<ul><li>Are almost impossible to evade since some can cover an area that is over 1 km wide.
</li><li>Fire a cone of destruction hitting victims multiple times, inflicting a huge multiplier on large targets.
</li><li>Often a source of friendly fire.
</li></ul>
<h3><span class="editsection">[<a href="/index.php?title=Weapons&amp;action=edit&amp;section=5" title="Edit section: Flak weapons">edit</a>]</span> <span class="mw-headline" id="Flak_weapons">Flak weapons</span></h3>
<ul><li>Are primarily designed as anti-fighter weapons.
</li><li>Create a large damage area capable of seriously damaging fighters.
</li></ul>
<h2><span class="editsection">[<a href="/index.php?title=Weapons&amp;action=edit&amp;section=6" title="Edit section: Damage">edit</a>]</span> <span class="mw-headline" id="Damage">Damage</span></h2>
<p>Damage is inflicted in two ways.
</p>
<h4><span class="editsection">[<a href="/index.php?title=Weapons&amp;action=edit&amp;section=7" title="Edit section: Hull">edit</a>]</span> <span class="mw-headline" id="Hull">Hull</span></h4>
<p>Hulls are the outer shells of ships, they usually protect the equipment that makes a ship a ship rather than a blasted out derelict.
Damage to the hull directly damages the ship and its equipment.
</p>
<h4><span class="editsection">[<a href="/index.php?title=Weapons&amp;action=edit&amp;section=8" title="Edit section: Shield">edit</a>]</span> <span class="mw-headline" id="Shield">Shield</span></h4>
<p><a href="/index.php/Shields" title="Shields">Shields</a> are a protective energy field which primarily keeps micrometeorites and other debris from harming the hull in the course of normal navigation. They also protect the hull from weapons fire. Typically the shields must first be overcome to damage a ship.
</p>
