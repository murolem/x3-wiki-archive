<h2><span class="mw-headline" id="Description">Description</span></h2>
<p>For more info. on the Hub's 6 gates and connection see the <a href="/index.php/Hub_plot" title="Hub plot" class="mw-redirect">Hub plot</a>.
When initially discovered, this sector will be deep in xenon territory and convoys are not uncommon. This can be rectified during the Hub plot.
</p>
