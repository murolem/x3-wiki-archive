<h2><span class="mw-headline" id="Overview">Overview</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Snail_Ranch&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>Paranid Snail Ranches produce <a href="/index.php/Maja_Snails" title="Maja Snails">Maja Snails</a>, used to make <a href="/index.php/Majaglit" title="Majaglit">Majaglit</a>. <a href="/index.php/Energy_Cells" title="Energy Cells">Energy Cells</a> are necessary for the functioning of these stations.
</p><p>NPC factories accept <a href="/index.php/Teladianium" title="Teladianium">Teladianium</a> as a secondary resource.
</p>
<h3><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Snail_Ranch&amp;action=edit&amp;section=2" title="Edit section: Encyclopedia Entry">edit</a><span class="mw-editsection-bracket">]</span></span></h3>
<p>In order to meet the increasing demand for Majaglit the Paranid have created a number of specialised snail farms dedicated to the breeding and farming of the Maja Snail. Inside these farms great artificial pools and rivers have been constructed to create an artificial environment where Maja Snails breed easily.
</p>
<h2><span class="mw-headline" id="Availability">Availability</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Snail_Ranch&amp;action=edit&amp;section=3" title="Edit section: Availability">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>This factory is unique to the Paranid.
</p>
