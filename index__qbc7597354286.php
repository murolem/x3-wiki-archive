==Description==
===Encyclopedia Entry===
This mining outpost is surrounded by a field of mineral bearing asteroids with a particularly dense asteroid field to the north. Pirate activity is light and generally non-aggressive, mostly transports headed for the north gate. This is fortunate as high-speed combat in this system is made difficult by the asteroids which also play havoc with missile targeting scanners.
===Information===
In Albion Prelude, Plutarch has moved their headquarters to operations to the Albion systems, along with, apparently, most of the asteroids in this sector.
