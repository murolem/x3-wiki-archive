<h3><span class="mw-headline" id="Fighting">Fighting</span></h3>
<p>Fighting skill can be trained only by successfully completing boarding operations. After each successful boarding, each marine that participated in the boarding increases his Fighting skill by a random amount between 5 to 9 points, like a Quick training course in the other three skills does. The success probability of boarding well-defended ships depends very greatly on the combined Fighting skill of the marines involved, so one should specifically train the marines' Fighting skill through practice boarding operations before taking on, say, Xenon or Terran vessels. Boardable pirate vessels and ships from <a href="/index.php/Return_Ship" title="Return Ship">Return Ship</a> missions are ideal for this: the former are quite easy to board, and the latter spawn without shield, weapons, or equipment, making them sitting ducks waiting to be boarded. After the boarding, the ship can be returned to its owner normally to complete the mission. One should send the largest boarding party the target ship can hold, to minimize the risk of losses and to maximize the number of marines trained. However, low Engineering skill can cause a large boarding party to deal too much damage to the hull and result in the boarding being aborted; in this case, a smaller boarding party may have to be used, excluding those marines with particularly poor Engineering skill.
</p>
