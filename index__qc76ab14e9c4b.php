<h2><span class="mw-headline" id="Training_Marines">Training Marines</span></h2>
<p>To reach peak effectiveness, Marines must be trained.
</p>
<h3><span class="mw-headline" id="Engineering.2C_Hacking_and_Mechanical">Engineering, Hacking and Mechanical</span></h3>
<p>Engineering, Hacking and Mechanical skills can all be improved at training facilities which are typically the same stations where they can be hired: <a href="/index.php/Military_Outpost" title="Military Outpost">Military Outposts</a>, <a href="/index.php/Military_Base" title="Military Base">Military Bases</a>, or <a href="/index.php/Marine_Training_Barracks" title="Marine Training Barracks">Marine Training Barracks</a>, but also at <a href="/index.php/Trading_Station" title="Trading Station">Trading Stations</a> and <a href="/index.php/Stock_Exchange" title="Stock Exchange">Stock Exchanges</a>. Training duration, cost, and effectiveness is the same everywhere.
