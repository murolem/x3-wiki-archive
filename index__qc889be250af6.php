<div id="mw-content-text" lang="en" dir="ltr" class="mw-content-ltr"><p>X3 is a game that simulates a fictional universe comprised of sectors of space connected by gates, the <a href="/index.php/X-universe" title="X-universe">X-universe</a>.
</p><p>As a concession to limited processor capabilities available to most home users, X3's simulation of the universe's processes are simplified when not in direct view. This leads to two modes of simulation known in the community <a href="/index.php/Abbreviations#General" title="Abbreviations">colloquially</a> as <b>IS</b> and <b>OOS</b>.
</p><p><b>IS</b>, either In-System or In-Sector, are things rendered within the same system or sector the player is in. Since the player can interact with them, they must be modeled as physical objects, subject to collisions. Time is evaluated in very fine chunks.
</p><p><b>OOS</b>, or Out-Of-Sector, are things that happen outside of the system the player is in. Since the player has no direct contact with these objects they can be simplified to statistical events, thus removing collision checking.
</p><p>The difference in modeling causes some unexpected behavior in OOS mode, as the objects no longer have to obey physical constraints and as for further computational savings, time is evaluated in larger chunks. Furthermore, OOS events the player is not directly monitoring through the Trading Extension occur in larger time chunks than those that are being directly monitored.
</p>
