<h2><span class="editsection">[<a href="/index.php?title=Simulation_modeling&amp;action=edit&amp;section=1" title="Edit section: Differences">edit</a>]</span> <span class="mw-headline" id="Differences">Differences</span></h2>
<table cellpadding="10">

<tr valign="top">
<td>
<h3><span class="editsection">[<a href="/index.php?title=Simulation_modeling&amp;action=edit&amp;section=2" title="Edit section: In-Sector">edit</a>]</span> <span class="mw-headline" id="In-Sector">In-Sector</span></h3>
<ul><li>Combat is modeled in real-time, accounting for projectiles, collisions, and orientation.
</li><li>Movement takes into account obstacles and potential collisions, and ships must maneuver around obstacles.
</li></ul>
</td>
<td>
<h3><span class="editsection">[<a href="/index.php?title=Simulation_modeling&amp;action=edit&amp;section=3" title="Edit section: Out-of-Sector">edit</a>]</span> <span class="mw-headline" id="Out-of-Sector">Out-of-Sector</span></h3>
<ul><li>Combat is simplified to a turn-based system considering the full installed weapon power of combatant ships.
</li><li>There is no collision avoidance and it is perfectly legal for two ships to occupy the same position in space.
</li></ul>
</td></tr></table>
<h2><span class="editsection">[<a href="/index.php?title=Simulation_modeling&amp;action=edit&amp;section=4" title="Edit section: Combat Example: Out-of-Sector">edit</a>]</span> <span class="mw-headline" id="Combat_Example:_Out-of-Sector">Combat Example: Out-of-Sector</span></h2>
<p>Turns run 30 seconds, 5s if monitored via sector map.
</p><p>50 Jaguars w/ 4 IREs &amp; 2 1MJ shields costing about 70k credits each.<br />
-vs-<br />
Python w/ lots of guns &amp; 3 2GJ shields costing over 90M credits
</p><p>4 IREs will cause 560 Hull/5.6 MJ Shield damage/sec, over 30s this is 16.8K/168MJ, for 50 this is 840K/8400MJ, on a 5s timestep this converts to 2.8K/28MJ per ship, and 140K/1400MJ for 50.
</p><p>A Jaguar has 2MJ in shielding and 800 hull points.<br />
If the Python is armed, it will do enough damage to kill a jaguar, unfortunately, OOS combat tends to not account for things like Area Effect weapons or permit targeting multiple ships like an M2 would ordinarily do against a fighter swarm.
</p><p>A Python has 6GJ in shields and 400K hull points.
</p><p>If you watch the fight on the sector map what will probably happen is the Python will kill 5 Jaguars while they pare down the shields and inflict a little hull damage. It will then kill another 4-5 while the survivors destroy the rest of the hull.
It will probably fare worse if you don't watch the fight.
</p><p>Result, Jaguars suffer approximately 20% casualties for complete destruction of the M2. This analysis neglects shield recharge which will tilt the outcome in the Python's favor, but not enough to win.
</p><p>The results of an In-Sector battle will go completely differently, especially if the Python is armed with Flak weapons. A number of the M5s are quite likely to collide with the M2 and cripple or destroy themselves. The Python's weapons fire will generally miss the small and maneuverable M5s. M5 traffic control will reduce the number of attackers that can effectively attack at a single time. Fighter maneuvers will reduce the time spent firing during each pass. It is most probable that the Jaguars will be completely annihilated without causing significant damage to the Python.
</p>
