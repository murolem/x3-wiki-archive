<h2><span class="editsection">[<a href="/index.php?title=Scripts_%26_Mods&amp;action=edit&amp;section=1" title="Edit section: Activation of the Script Editor">edit</a>]</span> <span class="mw-headline" id="Activation_of_the_Script_Editor">Activation of the <a href="/index.php/Script_Editor" title="Script Editor" class="mw-redirect">Script Editor</a></span></h2>
<p>To activate the SE, rename your pilot to "Thereshallbewings" (Look out for the capital T and double l that the key repeat rate hinders). If it worked correctly, the game will freeze for a short while, and when it comes back your name should be unchanged. Instead, you will now be able to access the script editor from the ship command console. (Shift-C, s in Terran Conflict)
</p><p>Once here you can see scripts the game is running, manually run scripts, edit or write your own, and view other additional information.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Scripts_%26_Mods&amp;action=edit&amp;section=2" title="Edit section: Activation of the Galaxy Editor">edit</a>]</span> <span class="mw-headline" id="Activation_of_the_Galaxy_Editor">Activation of the <a href="/index.php/Galaxy_Editor" title="Galaxy Editor">Galaxy Editor</a></span></h2>
<p>To activate the GE, rename your pilot to "Makeanapplepiefromscratch". You will find a new "<a href="/index.php/Starts" title="Starts">Game Start</a>" when you create a new game called "Galaxy Editor". It is found below "Custom Game". You will be prompted with two options: "Revelation", which is the Game galaxy in X3:AP and "&lt;New Map&gt;". If you modify "Revelation", you will change the map in which you are currently playing with your saves. (be careful)
</p><p>The Galaxy Editor enables you to do pretty much anything when it comes to creation. You can make new sectors, stations, Suns, Nebulas, Gates, Ships and Asteroids.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Scripts_%26_Mods&amp;action=edit&amp;section=3" title="Edit section: The Difference Between Scripts &amp; Mods">edit</a>]</span> <span class="mw-headline" id="The_Difference_Between_Scripts_.26_Mods">The Difference Between Scripts &amp; Mods</span></h2>
<p>Mods is often used as a term for any modifications in general, however scripts are specifically distinct from mods. Scripts are sections of code directly changing or adding to the game by running additional features, such as plugins. An example would be anything ranging from a plugin allowing complex combat or economic changes, to a simple 'give me money' cheat. Mods are the changes to any of the actual models or type files, examples include adding or changing ship or station models, compatible weaponry, changing gamestarts or the universe layout, or other graphical changes.
</p>
