<div id="mw-content-text" lang="en" dir="ltr" class="mw-content-ltr"><p>Warning, the following information concerns game modifications, which may cause problems.
</p><p>The <a href="/index.php/X-universe" title="X-universe">X-universe</a> games are almost endlessly moddable, and even have an ingame script editor, allowing players to do almost anything, from the ability to affect the inner workings of the game to something as basic as giving themselves some extra cash. Add to this several ship and station modelling programmes and you have some very good commuity-created content. There are so many, in  fact, that it would be foolish to try and fit them all on this page.  
</p><p>Thankfully, Egosoft maintains such a repository in their forums:
</p>
<ul><li> <a rel="nofollow" class="external text" href="http://forum.egosoft.com/viewforum.php?f=25">X2: The Threat</a>
</li><li> <a rel="nofollow" class="external text" href="http://forum.egosoft.com/viewforum.php?f=66">X3: Reunion</a>
</li><li> <a rel="nofollow" class="external text" href="http://forum.egosoft.com/viewforum.php?f=94">X3: Terran Conflict</a>
</li></ul>
<p>Mods will generally result in a game save to be marked as *modified*, as opposed to <a href="/index.php/Vanilla" title="Vanilla">Vanilla</a> games which are unmarked. Some simply graphical changes, such as low-polygon asteroids or the removal of complex tubes, do not trigger a modified tag.
</p><p><br />
</p>
