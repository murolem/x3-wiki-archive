<h2><span class="mw-headline" id="Overview">Overview</span></h2>
<p>The Photon Pulse Cannon or PPC is the premier <a href="/index.php/Commonwealth" title="Commonwealth">Commonwealth</a> <a href="/index.php/Category:Capital_Ships" title="Category:Capital Ships">capital ship</a> weapon.
</p>
<h2><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span></h2>
<p>A deadly weapon found on <a href="/index.php/M7" title="M7">frigates</a>, <a href="/index.php/M2" title="M2">destroyers</a> and <a href="/index.php/M1" title="M1">carriers</a>.
</p><p>First developed by Professor Jo Bydnah of the Argon, the Photon Pulse Cannon became a favorite of the Argon Military, who then sold the original design to other races. This type of weapon is extremely powerful, and can only be fitted on the larger Capital craft. It works by sending highly charged photon radiation through a series of coils, that multiplies the charge, creating a very deadly pulse of radiation.
</p>
<h2><span class="mw-headline" id="Commentary">Commentary</span></h2>
<p>The PPC does the job that the <a href="/index.php/High_Energy_Plasma_Thrower" title="High Energy Plasma Thrower">HEPT</a> once did; a weapon that capital ships use to slug it out with other capital ships. It is the most all-round of the capital weapons that include the <a href="/index.php/Point_Singularity_Projector" title="Point Singularity Projector">PSP</a>, <a href="/index.php/Ion_Cannon" title="Ion Cannon">Ion Cannon</a>, <a href="/index.php/Gauss_Cannon" title="Gauss Cannon">Gauss Cannon</a> and <a href="/index.php/Incendiary_Bomb_Launcher" title="Incendiary Bomb Launcher">IBL</a>. Whilst it can be charged for extra damage, the PPC deals enough that it isn't necessary. <a href="/index.php/M2" title="M2">Destroyers</a> are usually equipped with full arrays of them; but they set the owner back a pretty penny, as together a full loadout of PPCs is almost as costly as some of the destroyer hulls that mount them.
</p>
<h2><span class="mw-headline" id="Known_Compatible_Ships">Known Compatible Ships</span></h2>
<p>Almost all M1 and M2 class ships can use this weapon.<br />
The <a href="/index.php/Thresher" title="Thresher">Boron Thresher</a> and the <a href="/index.php/Q" title="Q">Xenon Q</a> are the only M7 class ships which can mount the PPC.<br />
Similarly, the Goner <a href="/index.php/Truelight_Seeker" title="Truelight Seeker">Truelight Seeker</a> is the only known M6 which can mount the PPC.
</p>
