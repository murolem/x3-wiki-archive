<p>A <a href="/index.php/Missions#Other_Mission" title="Missions">Mission</a> offered at <a href="/index.php/Stations" title="Stations">stations</a>.
</p><p>General difficulty: Easy in some cases, very hard in other cases. 
</p><p>This is a simple 'retrieve' mission you will need to do to find a ship, claim it and send it back to it's owner. However there is a twist to this mission favoured by many X-Gamers. You can claim it and NOT send it back...  . The downside of keeping the ship in one of these missions is that you take a big rep loss, because it's considered stealing. Aborting this mission, even if you haven't claimed the ship has the same result and the ship will jump away if you haven't claimed it. Shoot it down... rep loss. Abort mission... rep loss. Crash into it and blow it up... rep loss. Keep ship... rep loss. Have ship destroyed by someone else... 
</p><p><br /> 
And you will receive a small sum of money and no rep loss! 
</p><p><br />
</p><p>Self Destruct will have the same effect as having it destroyed by hostiles. - by Nanook. 
</p><p>So choose this mission only if you seriously plan to finish it or plan to keep the ship and suffer the consquences. 
</p><p>What you will need: 
</p>
<ul><li> A fast M5 or a ship with jumpdrive and energy cells. Signature Override Software is useful but illegal. 
</li><li> TM class ship with Jumpdrive and energy cells to dock the ship to when you've claimed it. - by Omini. 
</li></ul>
<p>What you will find: 
</p><p>In the beginning of the game maybe a small M5 or M4 fighter. 
Later on you can get as much as an M7 of M8! - Sometimes keeping the ship is better worth it than getting the mission reward.  
</p><p>Oooh, and if you fail mission
</p><p>you will get some nice sector police spawn dedicated to destroy it...
</p>
