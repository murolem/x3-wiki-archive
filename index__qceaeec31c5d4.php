<h2><span class="mw-headline" id="Overview">Overview</span></h2>
<p>Advanced Satellite Factories produce <a href="/index.php/Advanced_Satellite" title="Advanced Satellite" class="mw-redirect">Advanced Satellites</a> from <a href="/index.php/Energy_Cells" title="Energy Cells">Energy Cells</a>, <a href="/index.php/Majaglit" title="Majaglit">Majaglit</a> and <a href="/index.php/Silicon_Wafers" title="Silicon Wafers">Silicon Wafers</a>. 
</p><p>NPC stations will buy <a href="/index.php/Computer_Components" title="Computer Components">Computer Components</a> as a secondary resource.
</p>
<h3><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span></h3>
<p>This station manufactures advanced satellites. In addition to the regular navigation relay satellite, advanced satellites feature video capture technology. The factory uses delicate machinery to build these small, complex devices.
</p>
