<td>Tanker
<p>(larger version)	
</p>
</td><td>Even slower, more shielding, more cargo space
</td></tr>
<tr>
<td>Super-freighter	</td><td>Even slower, more shielding, more cargo space
</td></tr>
<tr>
<td>Super-freighter
<p>(larger version)	
</p>
</td><td>Even slower again, even more shielding, even more cargo space
</td></tr></table>
</td></tr>
