<h3><span class="editsection">[<a href="/index.php?title=Abbreviations&amp;action=edit&amp;section=2" title="Edit section: Ship Variants">edit</a>]</span> <span class="mw-headline" id="Ship_Variants">Ship Variants</span></h3>
<p>For smaller ships, most races have some variants of the basic ship types. Not all races have all variants available to buy at ship yards, and some don't even exist in a normal game.
</p>
<table cellspacing="0" cellpadding="10">

<tr valign="top">
<td>
<p>Fighters can have up to four variants, typically having the following characteristics<br />
(though there is some variation so these rules do not always hold).
</p>
<table border="1" cellspacing="0" cellpadding="2">

<tr>
<td>Raider	</td><td>Faster, less shielding and less cargo
</td></tr>
<tr>
<td>Vanguard	</td><td>Faster, sometimes less shielding, sometimes better weapons
</td></tr>
<tr>
<td>Hauler	</td><td>Slower, more cargo space
</td></tr>
<tr>
<td>Sentinel	</td><td>Slower, more shielding, sometimes less cargo space
</td></tr></table>
</td><td>
<p>Small freighters may also have variants, again depending on race and again with not all variants being available for all ships.
</p>
<table border="1" cellspacing="0" cellpadding="2">

<tr>
<td>Hauler	</td><td>Slower, more shielding, more cargo space
</td></tr>
<tr>
<td>Miner	</td><td>Slower, more shielding, more cargo space
</td></tr>
<tr>
<td>Tanker	</td><td>Slower, more shielding, more cargo space
</td></tr>
