<div id="mw-content-text" lang="en" dir="ltr" class="mw-content-ltr"><p>This page is mostly for my own use, but as I don't visit here all that often any more, please feel free to butt in on any of the pages mentioned below. If you do, leaving a message on my discussion page would be appreciated, but of course is not necessary.
</p><p>I'm largely responsible for the initial Racial Navboxes, but other people (notably <a href="/index.php/User:Knighster35" title="User:Knighster35">Knighster35</a>) have also done plenty of work on them.
</p><p>I started work on a series of Guides to keep on the wiki, but never got around to finishing them - while I may pick them up again in the future, <a href="/index.php/Template:Trading" title="Template:Trading">Template:Trading</a> was my primary goal to finish. <a href="/index.php?title=Guides&amp;action=edit&amp;redlink=1" class="new" title="Guides (page does not exist)">Guides</a> could do with being populated, but without a few more, it seems rather pointless.
</p><p><br />
My general suggestion, is to contribute where you can, and if the Admins ever start giving out privileges to upload screenshots, get them to shoot me an E-mail, because I would love to upload some pictures of the various ship types.
</p><p>Have fun editing. 
</p>
