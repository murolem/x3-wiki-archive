<h2><span class="mw-headline" id="Description">Description</span></h2>
<p>Bathed in the sacred light of the sun 'Sonra', The Argon home system is dominated by the planet 'Argon Prime'. This rich and beautiful world is densely populated and is usually protected by a large Argon Military force, a <a href="/index.php/Titan_(ship)" title="Titan (ship)">Titan Destroyer</a> and more recently the Argon's latest flagship carrier, the Argon One, a <a href="/index.php/Colossus" title="Colossus">Colossus class Carrier</a>. Most of the needs of the Argon space fleet are catered for here in orbiting shipyards that also outfit for private fighters and transport ships with Argon technology.
</p><p><br />
</p>
