<a name="General" id="General"></a><h1><span class="editsection">[<a href="/index.php?title=Argon&amp;action=edit&amp;section=1" title="Edit section: General">edit</a>]</span> <span class="mw-headline"> General </span></h1>
<a name="Government_and_politics" id="Government_and_politics"></a><h2><span class="editsection">[<a href="/index.php?title=Argon&amp;action=edit&amp;section=2" title="Edit section: Government and politics">edit</a>]</span> <span class="mw-headline"> Government and politics </span></h2>
<p>The Argon Federation is a democratic society where each city and space station (factory) is represented by a Senator. Each senator is elected by simple plurality in the district. The President is also elected by simple plurality in the entire Federation. The current president of the Argon Federation is Frann Herron.
</p>
<a name="Foreign_relations_and_military" id="Foreign_relations_and_military"></a><h3><span class="editsection">[<a href="/index.php?title=Argon&amp;action=edit&amp;section=3" title="Edit section: Foreign relations and military">edit</a>]</span> <span class="mw-headline"> Foreign relations and military </span></h3>
<p>The Argon and Boron share an undefended border, co-operate on military exercises and technological advances, and are each others largest trading partner. Nevertheless, the Argon Federation also has its own foreign policy specifically with the Paranid Empire with whom the Argon has cordial but strained relations.
</p><p>An industrialized society, the Argon currently have one of the most well-rounded ships in the known universe which has made the Argon Fleet formidable in its own right. Since the Xenon Conflict, the Argon have produced a wide variety of ships from fast scouts to large carriers for its fleet, the most notable of which is the Argon One that patrols Argon Prime. These ships are also available for purchase by private individuals or corporations assuming one has the proper authorization and enough credits. 
</p>
<a name="History" id="History"></a><h2><span class="editsection">[<a href="/index.php?title=Argon&amp;action=edit&amp;section=4" title="Edit section: History">edit</a>]</span> <span class="mw-headline"> History </span></h2>
<p>The Argon are the descendants of the fleet of daring pilots commanded by <a href="/index.php?title=Nathan_R._Gunne&amp;action=edit&amp;redlink=1" class="new" title="Nathan R. Gunne (page does not exist)">Nathan R. Gunne</a> that led the Terraformer fleet away from Earth and through the subsequently destroyed Earth jumpgate, trapping both fleets in the gate network. When it became apparent that they were never returning home, the survivors started to rebuild their lives and eventually began to collectively call themselves "Argon" in remembrance of Gunne's sacrifice. It would be many centuries before the Argon would know of the term <a href="/index.php/Terran" title="Terran">Terran</a> again.
</p>
<a name="Culture" id="Culture"></a><h2><span class="editsection">[<a href="/index.php?title=Argon&amp;action=edit&amp;section=5" title="Edit section: Culture">edit</a>]</span> <span class="mw-headline"> Culture </span></h2>
<a name="Geography_and_Climate" id="Geography_and_Climate"></a><h2><span class="editsection">[<a href="/index.php?title=Argon&amp;action=edit&amp;section=6" title="Edit section: Geography and Climate">edit</a>]</span> <span class="mw-headline"> Geography and Climate </span></h2>
<p>Genetically identical to Terrans, Argon worlds range from the desert planet Sandwell in <a href="/index.php/Antigone_Memorial" title="Antigone Memorial">Antigone Memorial</a>, to the Earth-like planet <a href="/index.php/Omicron_Lyrae" title="Omicron Lyrae">Omicron Lyrae</a>. 
</p><p><br />
</p>
<a name="Science_and_Technology" id="Science_and_Technology"></a><h1><span class="editsection">[<a href="/index.php?title=Argon&amp;action=edit&amp;section=7" title="Edit section: Science and Technology">edit</a>]</span> <span class="mw-headline"> Science and Technology </span></h1>
<a name="Ships" id="Ships"></a><h2><span class="editsection">[<a href="/index.php?title=Argon&amp;action=edit&amp;section=8" title="Edit section: Ships">edit</a>]</span> <span class="mw-headline"> Ships </span></h2>
<p>Argon ships are considered the most well-rounded ships in the X universe. While their designs are partially inspired by the Boron Kingdom, they do not share the peaceful nature of the Boron. As such, their ships may mount more aggressive weapons such as the Mass Driver, which completely ignores shields. The Mass Driver was developed according to some very old re-discovered documents. It's performance was so impressive, the Split Dynasty paid enormous fees for the designs so they could manufacture it as well. 
</p>
