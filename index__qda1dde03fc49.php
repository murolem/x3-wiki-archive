<p>The Argon are one of the <a href="/index.php/Races" title="Races">races</a> in the X Universe, collectively known as the Argon Federation. They share borders with the Kingdom of Boron and the Paranid Empire with several outlier sectors sharing borders with the Split Dynasty and Teladi Company.
</p><p>The Argon Federation is governed by a democratic body with every city and space station being represented with its own senator. The leader of the Federation is known as the President. They are a member of the <a href="/index.php/Races#Community_of_Planets" title="Races">Community of Planets</a>, a founding member of the <a href="/index.php/Races#Foundation_Guild" title="Races">Foundation Guild</a> and allies with the <a href="/index.php/Boron" title="Boron">Kingdom of Boron</a>.
</p>
