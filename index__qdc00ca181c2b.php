<div lang="en" dir="ltr" class="mw-content-ltr"><p>M3+ ships are heavy fighters.<br />
A recent development based on Bala Gi's research. The M3+ class has found a home in all Commonwealth fleets.
</p><p>Note that an M3+ designation does not exist in the game. These ships are classified as <a href="/index.php/M3" title="M3">M3</a> in-game, however they are slower, more potent and better defended than their counterparts so they are given an alternate designation in the community.
</p><p>Members of this ship class include:
