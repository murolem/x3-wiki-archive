<h2><span class="mw-headline" id="Availability">Availability</span></h2>
<p>The Ranger cannot be purchased at any shipyard.  However, it may be <a href="/index.php/Bail" title="Bail">captured</a>.
</p><p>It is also the starting ship of the <a href="/index.php/Goner_Witness" title="Goner Witness" class="mw-redirect">Goner Witness</a> gamestart.
</p>
<h2><span class="mw-headline" id="Suggested_Roles">Suggested Roles</span></h2>
<p>Due to its respectable L-class cargo bay, the Ranger is sometimes used as a light freighter in much the same way as a the <a href="/index.php/Falcon" title="Falcon">Falcon</a> and <a href="/index.php/Kea" title="Kea">Kea</a> are used.  This appears to be its only real use.
</p>
