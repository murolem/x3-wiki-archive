</p>
<h2><span class="mw-headline" id="Physical_Characteristics">Physical Characteristics</span></h2>
<p>The Ranger closely resembles the <a href="/index.php/Argon" title="Argon">Argon</a> <a href="/index.php/Nova" title="Nova">Nova Sentinel</a>, though as previously noted it has no weapons hardpoints.
</p>
<h2><span class="mw-headline" id="Combat">Combat</span></h2>
<p>Flying a Ranger in combat involves exactly one "tactic":  running away.
</p>
