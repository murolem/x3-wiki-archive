<h2><span class="mw-headline" id="Overview">Overview</span></h2>
<p>In keeping with their pacifistic creed, the standard Goner ship, designated the <b>Ranger</b>, is unarmed.  As such it is in its own <a href="/index.php/Category:Goner" title="Category:Goner">category</a> in the <a href="/index.php/X-universe" title="X-universe" class="mw-redirect">X-universe</a>.
</p>
<h2><span class="mw-headline" id="Background">Background</span></h2>
