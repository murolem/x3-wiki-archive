<h1><span class="mw-headline" id="General">General</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Split&amp;action=edit&amp;section=1" title="Edit section: General">edit</a><span class="mw-editsection-bracket">]</span></span></h1>
<p>They are humanoid creatures with more physical strength than an average human. They are a warrior race ruled over by a dominant family. In X3: Reunion, the dominant family was Rhonkar.  Their territory includes their home sector Family Pride and other sectors.
</p>
<h2><span class="mw-headline" id="Government_and_politics">Government and politics</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Split&amp;action=edit&amp;section=2" title="Edit section: Government and politics">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<h3><span class="mw-headline" id="Foreign_relations_and_military">Foreign relations and military</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Split&amp;action=edit&amp;section=3" title="Edit section: Foreign relations and military">edit</a><span class="mw-editsection-bracket">]</span></span></h3>
<p>The Split are enemies with the Boron due to their conflicting ideologies with each other. Relations with the Argon race are strained because of the latter's alliance with the Boron. They are allied with the Paranid, although their relationship is shaky at best. They are members of the Profit Guild and are neutral with the <a href="/index.php/Teladi" title="Teladi">Teladi</a>.
</p>
<h2><span class="mw-headline" id="History">History</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Split&amp;action=edit&amp;section=4" title="Edit section: History">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>About 450 years before the events of X: Beyond the Frontier, the Split Dynasty had destroyed most of the Boron colonies, forcing them back to Kingdom End.  The Argon Federation helped the Boron repel an attack on their home world and officially entered the war themselves. The Paranid Empire sent ships to supplement Split warships in an attack on Argon Prime. This war is now known as the <a href="/index.php/Boron_Campaign" title="Boron Campaign">Boron Campaign</a>. <br />
The Split continue to harbor animosity and outright hostility towards the Boron. In preparation for resumed hostilities, the Split are currently engaged in a military build up in the new Colony Areas consisting of: <a href="/index.php/Contorted_Dominion" title="Contorted Dominion">Contorted Dominion</a>, <a href="/index.php/Thyn%27s_Excavation" title="Thyn's Excavation">Thyn's Excavation</a>, and <a href="/index.php/Patriarch%27s_Conclusion" title="Patriarch's Conclusion">Patriarch's Conclusion</a>. The Boron are also building up their military presence in their new colonies of: <a href="/index.php/Faded_Dreams" title="Faded Dreams">Faded Dreams</a>, <a href="/index.php/Queen%27s_Harbour" title="Queen's Harbour">Queen's Harbour</a>, <a href="/index.php/Menelaus%27_Oasis" title="Menelaus' Oasis">Menelaus' Oasis</a>, and <a href="/index.php/Queen%27s_Retribution" title="Queen's Retribution">Queen's Retribution</a>.
</p><p>The Split are the only race to have had contact with the Xenon, something which has caused distrust with many, especially the Argon. 
</p><p>The Split Dynasty, Teladi Company and Paranid Empire form the <a href="/index.php/Profit_Guild" title="Profit Guild">Profit Guild</a>.
</p>
<h2><span class="mw-headline" id="Culture">Culture</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Split&amp;action=edit&amp;section=5" title="Edit section: Culture">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>The Split are more of a clan-centered conglomerate than anything; political power and influence is often determined by the strength of a Split's family, and their sectors are often named after the families that dominate them. They clearly have a preference for battle, and seem to have little ethical concern over their actions, as evidenced by their ongoing attempts to eradicate the Boron, their legalization of the harvesting of endangered space flies, and their stated extensive use of slave labor in their mines.
</p>
<h2><span class="mw-headline" id="Geography_and_Climate">Geography and Climate</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Split&amp;action=edit&amp;section=6" title="Edit section: Geography and Climate">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<h1><span class="mw-headline" id="Science_and_Technology">Science and Technology</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Split&amp;action=edit&amp;section=7" title="Edit section: Science and Technology">edit</a><span class="mw-editsection-bracket">]</span></span></h1>
<p>Their ships are noticeably focused more on weapons than shields. Their ships supposedly use the Markus Space Fly for power although this is against the law in all other governments of the Community of Planets. The Split use Boron made Ion Disruptors to stun the Space Flies before capture.
</p>
<h3><span class="mw-headline" id="Ships">Ships</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Split&amp;action=edit&amp;section=8" title="Edit section: Ships">edit</a><span class="mw-editsection-bracket">]</span></span></h3>
<p>Split ships are fast, powerful and well armed, but famously lacking in shielding. Oddly, they tend to also lack the ability to sustain fire for long periods of time. For these reasons, Split ships are ideal for using a series of fast attacks and retreats, or intercepting slower, weaker ships.
