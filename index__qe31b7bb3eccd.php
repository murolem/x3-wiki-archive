<div lang="en" dir="ltr" class="mw-content-ltr"><p>M6 ships are corvettes frequently found leading fighter patrols or escorting capital ships. They are too big to be considered to be fighters, but too small to be considered true <a href="/index.php/Capital_ship" title="Capital ship" class="mw-redirect">capital ships</a>.
</p><p>More boat than ship, corvettes are manned by a small crew, and they can carry quite powerful weapons. They are several times the size of a <a href="/index.php/M3" title="M3">M3</a> ship (typically 120-280m long) and most have a lot more shields. They are not as maneuverable as fighters and therefore need to use their power and shielding to take down enemy fighters, as their speed and turning is usually not adequate to dogfight.
</p><p>Most M6 class ships have both main guns and a few turrets. Some of the <a href="/index.php/M6%2B" title="M6+">M6+</a> ships, recognisable from their regular counterparts by the moniker 'Heavy' in their name, can mount the most powerful corvette weapons in all turrets but they are generally slower then the normal M6s.
</p><p>The M6s are the class right in the middle of the ship scale, and are generally regarded to be capable of taking on anything with suitable pilot skill, from M5 swatting to M2 strafing. As such, they are commonly used as player ships.
</p><p>Regular M6s listed below tend to be better suited to taking on small, manoeuvrable fighters, whilst their M6+ cousins tend to mount heavier weapons to deal with bigger ships.
</p><p>Examples of M6 class ships are;
</p>
