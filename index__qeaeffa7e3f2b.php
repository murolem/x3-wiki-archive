<h2> <span class="mw-headline" id="Overview">Overview</span></h2>
<p>The Thor is quick and packs a punch. However, in all but the latest versions of the game, it cannot be obtained by the player.
</p>
<h2> <span class="mw-headline" id="Background">Background</span></h2>
<h2> <span class="mw-headline" id="Physical_Characteristics">Physical Characteristics</span></h2>
<h2> <span class="mw-headline" id="Combat"> Combat </span></h2>
<p>The Thor is very similar to the <a href="/index.php/Scimitar" title="Scimitar">Scimitar</a> in its characteristics. However, it packs an extra two weapons in the front and an additional turret, giving it even more potency. A serious threat to any fighter, including <a href="/index.php/M3%2B" title="M3+">M3+</a> class ships, and can only be easily taken down when outclassed.
</p>
<h2> <span class="mw-headline" id="Equipment"> Equipment </span></h2>
<h2> <span class="mw-headline" id="Availability"> Availability </span></h2>
<p>The Thor is offered as a potential plot reward for the Balance of Power plotline. It is otherwise unobtainable.
</p>
