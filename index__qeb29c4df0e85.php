<h2><span class="mw-headline" id="Overview">Overview</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Plankton_Farm&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>Plankton Farms are Boron factories that produce <a href="/index.php/Plankton" title="Plankton">Plankton</a> from <a href="/index.php/Energy_Cells" title="Energy Cells">Energy Cells</a>. 
</p><p>NPC stations will buy <a href="/index.php/Delexian_Wheat" title="Delexian Wheat">Delexian Wheat</a> as a secondary resource.
</p>
<h3><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Plankton_Farm&amp;action=edit&amp;section=2" title="Edit section: Encyclopedia Entry">edit</a><span class="mw-editsection-bracket">]</span></span></h3>
<p>Despite Boron Plankton being formed naturally on the Boron Home Planet, such is the popularity of Boron Spices that the Boron have established a large number of specialised Plankton farms to meet the demand. 
</p><p>These farms are large geodesic domes that create the unique chemical soup that is the atmosphere of the Planet Boron. Once produced the Plankton is then transported to a nearby Space Stott Mixery where it is processed into the many different Boron Spices. These factories also add the growing Plankton to Delexian Wheat, Meatsteak Cahoonas and Soja Husk in experiments to produce different varieties of foodstuff.
</p>
