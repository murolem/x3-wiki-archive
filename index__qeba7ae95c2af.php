<div id="mw-content-text" lang="en" dir="ltr" class="mw-content-ltr"><p>M4+ ships are light fighters, slower, with heavier armaments and defences than the regular M4s, but they usually don't fit the interceptor role. One-on-one they are often woefully inadequate for taking on <a href="/index.php/M3" title="M3">M3</a> ships in combat, having worse firepower and and much worse durability, while often not being any faster. However, they are much cheaper to procure, costing a good deal less than typical <a href="/index.php/M3" title="M3">M3s</a> and a third or a quarter the price of <a href="/index.php/M3" title="M3">M3</a>+ ships. This makes them good alternatives to <a href="/index.php/M3" title="M3">M3s</a> for fighter fleets at the cost of some potency. They constitute lethal symbiotes for <a href="/index.php/M1" title="M1">carrier</a> craft.
</p><p>Note that an M4+ designation does not exist in the game. These ships are referred to as <a href="/index.php/M4" title="M4">M4</a> class ships in-game, however they are slower, more potent and better defended than their counterparts so they are given an alternate designation in the community.
</p>
