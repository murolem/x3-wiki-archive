<h2><span class="mw-headline" id="Overview">Overview</span></h2>
<p>The Mass Driver or MD is an ammunition based weapon that pierces <a href="/index.php/Shields" title="Shields">shields</a> to deal damage directly to the hull of the target.
</p><p>MDs require <a href="/index.php/Mass_Driver_Ammunition" title="Mass Driver Ammunition">Mass Driver Ammunition</a> to fire.
</p>
<h2><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span></h2>
<p>This weapon is unique in that it is the only type of non-energy based projectile weapon available.
</p><p>It consists of a rapid feed multi barrelled cannon, which is capable of delivering a large number of small projectiles to its target in a short period of time. It inflicts damage not due to power of individual projectiles but because of the large number of projectiles delivered.
</p><p>Due to the unique nature of the weapon's ammunition it has almost no effect on a ship's shields but instead delivers heavy damage directly to the target's hull. It also causes very little power drain due to the fact that its ammunition supplies all offensive requirements. Currently the weapon can only be fitted on Argon and Split ships as they are the only ships that can accommodate the ammunition feed system.
</p><p>The true power of this weapon comes from its ammunition.
</p>
<h2><span class="mw-headline" id="Commentary">Commentary</span></h2>
<p>The high bullet velocity and shield piercing properties of its shots pose an unusually high threat to space vessels and make it fairly easy to use. 
The MD's high <a href="/index.php/Rate_of_Fire" title="Rate of Fire">ROF</a> grants it respectable <a href="/index.php/Damage_per_second" title="Damage per second">DPS</a> which steadily accumulates on a target's hull. If fielded in larger fleet level numbers, the MD can destroy the hulls of even large capital ships within minutes of sustained fire.
Because they do damage directly to the hull, MDs can immediately begin crippling targets making them slow enough to bring less nimble weapons into play. For the same reason they are terrible for <a href="/index.php/Capping" title="Capping" class="mw-redirect">capping</a> small craft, as they will destroy the hull without first depleting the shields.
</p><p>The primary limitations of MDs is cargo space for its ammo and fairly its short range. MDs are most effective against vessels with low hull strength and a reliance on either speed or shielding to keep them safe.
</p>
