<h2><span class="mw-headline" id="Equipment_Multiplication">Equipment Multiplication</span></h2>
<p>It is possible to exploit the game mechanics at shipyards to multiply rare, purchasable items. You still have to pay for each item you get, and only items sold at the shipyard can be multiplied, but the trick is useful for equipping ships with, say, rare Aldrin weapons or hard-to-come-by missiles.
</p><p>Multiple identical ships with identical equipment can be equipped together at shipyards, under the "Equip <i>x</i> ships at once" option. An item purchased this way is installed on each ship, with purchase cost correctly multiplied by <i>x</i>, but the shipyard's stock decreases by only one. Thus, it is possible to equip, for example, two ships at once with two <a href="/index.php/Prototype_Starburst_Shockwave_Cannon" title="Prototype Starburst Shockwave Cannon">Prototype Starburst Shockwave Cannons</a>, have each ship individually sell one back to the shipyard (so that each ship still has one left, and the shipyard has 2 in stock again), then buy two PSSC at once for both ships again, etc.
</p><p>Unpurchasable items cannot be multiplied, as they simply disappear when sold to a dock.
</p>
