<h2><span class="mw-headline" id="Overview">Overview</span></h2>
<p>Shipyards are gigantic spacebourne installations that specialise in the manufacture of spaceships and space stations.
</p><p>Despite the huge costs involved with running such an installation, each race has several dotted throughout their space, but not always one to each block. Each race also maintains at least one Super Shipyard, which is larger and manufactures a wider range of ships and stations than a standard Shipyard.
</p><p>Shipyards are typically found in the same Sector as an <a href="/index.php/Equipment_Dock" title="Equipment Dock">Equipment Dock</a> which is linked to the Shipyard. Any new vessels bought at a Shipyard may also be equipped straight off with any upgrades and wares currently stocked in the linked Equipment Dock.
</p><p>For a list of all stations produced for a particular race, see the race Shipyard pages for <a href="/index.php/Federal_Argon_Shipyard" title="Federal Argon Shipyard">Federal Argon Shipyard</a>, <a href="/index.php/Royal_Boron_Shipyard" title="Royal Boron Shipyard">Royal Boron Shipyard</a>, <a href="/index.php/Paranid_Shipyard" title="Paranid Shipyard">Paranid Shipyard</a>, <a href="/index.php/Split_Shipyard" title="Split Shipyard">Split Shipyard</a>, <a href="/index.php/Teladi_Shipyard" title="Teladi Shipyard">Teladi Shipyard</a> and <a href="/index.php/Terran_Shipyard" title="Terran Shipyard">Terran Shipyard</a>.
</p><p>Some races have factions that have their own shipyards like: <a href="/index.php/OTAS_Shipyard" title="OTAS Shipyard">OTAS Shipyard</a>, <a href="/index.php/Yaki_Shipyard" title="Yaki Shipyard">Yaki Shipyard</a> and <a href="/index.php/Aldrin_Ship_Production_Base" title="Aldrin Ship Production Base">Aldrin Ship Production Base</a>.
</p>
<h2><span class="mw-headline" id="Ship_Repair">Ship Repair</span></h2>
<p>In addition to construction, shipyards make repairs to damaged ships, restoring hull rating up to full potential.
