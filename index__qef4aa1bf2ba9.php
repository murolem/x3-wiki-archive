<h2><span class="editsection">[<a href="/index.php?title=Microchips&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a>]</span> <span class="mw-headline" id="Overview">Overview</span></h2>
<h3><span class="editsection">[<a href="/index.php?title=Microchips&amp;action=edit&amp;section=2" title="Edit section: Encyclopedia Entry">edit</a>]</span> <span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span></h3>
<p>Microchips are manufactured from silicon wafers that are especially treated with various coatings to create the required conductivity. They are mainly used in the production process of computer components.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Microchips&amp;action=edit&amp;section=3" title="Edit section: Production">edit</a>]</span> <span class="mw-headline" id="Production">Production</span></h2>
<p>Microchips are made at a <a href="/index.php/Chip_Plant" title="Chip Plant">Chip Plant</a>. They require <a href="/index.php/Silicon_Wafers" title="Silicon Wafers">Silicon Wafers</a>, <a href="/index.php/Energy_Cells" title="Energy Cells">Energy Cells</a> and <a href="/index.php/Wares#Food" title="Wares">Food</a>.
</p>
<h2><span class="editsection">[<a href="/index.php?title=Microchips&amp;action=edit&amp;section=4" title="Edit section: Commentary">edit</a>]</span> <span class="mw-headline" id="Commentary">Commentary</span></h2>
<p>The <a href="/index.php/Hub_plot" title="Hub plot">Hub plot</a> requires 75500 Microchips. It is wise to set up a production facility for them, preferably near a <a href="/index.php/Silicon_Mine" title="Silicon Mine">Silicon Mine</a>.
</p>
