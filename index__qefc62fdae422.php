==Overview==
A powerful carrier in its own right, the J has more weapons than most destroyers, though not the generator to use them. It has a very wide array of weapon options however, including the powerful ammo-based Matter/Antimatter Launcher in some turrets. It is quite slow for its class, with average shielding. Despite this, it can be surprisingly difficult to kill, particularly if accompanied by a fighter or corvette wing due to its ability to cause a large amount of damage in a short period.

