== Availability ==

The J can be found all over the universe in [[Xenon]] sectors, for those insane enough to try and [[boarding|pirate]] one. For those who wish to buy one, they can theoretically be purchased from the Xenon Shipyards... but as the Xenon hate everyone, it's impossible to buy one.

== Suggested Roles==
Capable of fleet support if you can capture one, the J makes for a great battle carrier due to its wide range of attacking options. A reasonably large hangar makes it lethal with a fighter wing, since it has good synergy with smaller, more agile units which complement its poor speed. Should never, like all carriers, be pitted against a destroyer. It is likely to lose.

