<h2><span class="mw-headline" id="Overview">Overview</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Tractor_Beam&amp;action=edit&amp;section=1" title="Edit section: Overview">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>A very useful item, the TB allows station repositioning, most helpful when complexing. It can be used by most <a href="/index.php/M6" title="M6">M6</a> class and above, and has no combatative worth.
</p>
<h2><span class="mw-headline" id="Encyclopedia_Entry">Encyclopedia Entry</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Tractor_Beam&amp;action=edit&amp;section=2" title="Edit section: Encyclopedia Entry">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>The Tractor Beam is a new invention, designed primarily to move stations without the need for large and clumsy grappling systems. The beam uses a series of projected gravitic fields, which in effect reduce the mass of the object, making it easier to move.
</p>
<h2><span class="mw-headline" id="Commentary">Commentary</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Tractor_Beam&amp;action=edit&amp;section=3" title="Edit section: Commentary">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>The Tractor Beam is commonly used to assemble mines into complexes. The idea of using it to tow asteroids into enemy capital ships is frequently touted, but this cannot be done (unless the asteroid is a mine) as the tractor beam cannot lock on to an object not owned by the player.
</p><p>While technically "compatible", the tractor beam cannot actually be used by ships smaller than M6 corvettes, due to the energy requirements. 
From <a class="external text" href="http://forum.egosoft.com/viewtopic.php?p=2805572">Nanook's post on the Egosoft X3 forum</a>&#160;:
<i>"Tractor beams are really designed to be used by M6's, TL's and larger ships. The fact that they fit on a Mercury is probably due to the fact that all 'special weapons' are lumped together - tractor beam, repair laser, mobile drilling system (mining laser). If it can mount one, it can mount them all. Most likely, that Mercury was designed to mount a MDS, so it can also mount, but not use, the TB. In short, get an M6."</i>
</p>
