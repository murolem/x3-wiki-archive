<h1><span class="mw-headline" id="General">General</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Teladi&amp;action=edit&amp;section=1" title="Edit section: General">edit</a><span class="mw-editsection-bracket">]</span></span></h1>
<p>The Teladi are a reptiloid race.
</p><p>Their main concern is for profit, and for that reason their ships use old and outdated technology they've traded for. Their ships are slow but heavily shielded.
</p><p>The Teladi have reasonable relations with all the other main races, including the <a href="/index.php/Pirates" title="Pirates">Pirates</a>, as certain Narcotics are grown legally in Teladi space, and bought up by smugglers.
</p>
<h2><span class="mw-headline" id="Government_and_politics">Government and politics</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Teladi&amp;action=edit&amp;section=2" title="Edit section: Government and politics">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<h3><span class="mw-headline" id="Foreign_relations_and_military">Foreign relations and military</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Teladi&amp;action=edit&amp;section=3" title="Edit section: Foreign relations and military">edit</a><span class="mw-editsection-bracket">]</span></span></h3>
<p>It is rumored that the Teladi provide pirates with much of their equipment, and this is evidenced by the extensive appearance of Teladi ships in pirate hands. Being money-oriented, the Teladi maintain good relations with most races in the X-Universe and take a neutral stance in military conflicts outside of their own space. Most Teladi ships have poor combat performance, making the Teladi less effective at defending their sectors from aggression.
</p>
<h2><span class="mw-headline" id="History">History</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Teladi&amp;action=edit&amp;section=4" title="Edit section: History">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>The Teladi have been plying the space lanes for longer than any of the other races, however following the <a href="/index.php/Terraformer_War" title="Terraformer War">Terraformer War</a> and the reorganisation of the jumpgate network, the far-flung and backwards Teladi colony of <a href="/index.php/Seizewell" title="Seizewell">Seizewell</a> was cut off from the rest of Teladi space and isolated amongst the other races who had had contact with the Terraformers.
</p><p>Without the technological base to sustain them, and with no desire to expend profits to once again reach the stars, it was nearly six centuries before the Teladi colony re-established itself in space. It ultimately only did so on discovering other species in the X-Universe, who provided the Teladi with the biggest profit opportunity in their history. In just two centuries, the new Teladi Company, headquartered at Seizewell, exploded into the dominating economic force in the X-Universe.
</p><p>Such was the success of the colony that when the X-Universe was reconnected to the jumpgate network following the events of the <a href="/index.php/Second_Xenon_Conflict" title="Second Xenon Conflict">Second Xenon Conflict</a>, Seizewell took over from the Teladi homeworld of Ianamus Zura as the de facto capital of all Teladi space.
</p>
<h2><span class="mw-headline" id="Culture">Culture</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Teladi&amp;action=edit&amp;section=5" title="Edit section: Culture">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<p>The Teladi are obsessed with making profits as racially possible. Their society is organized as one very large conglomerate and is lead by a mysterious figure known as Ceo. The profiteering attitude that makes Teladi infamous is actually only true of space-faring ones. Teladi that reside in their planets are just as philosophical and/or cynical as any human would be. Male Teladi often do not leave their homeworld of Ianamus Zura since unfertilized, unsupervised eggs would always hatch as female. As a result, the vast majority of Teladi flying in space are female. However, there do exist male Teladi pilots in space and not all space-faring Teladi are concerned with making profits to the fullest; some can be seen as surprisingly able fighter pilots and/or keen captains of naval capital ships worthy of the respect of other races.
</p>
<h2><span class="mw-headline" id="Geography_and_Climate">Geography and Climate</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Teladi&amp;action=edit&amp;section=6" title="Edit section: Geography and Climate">edit</a><span class="mw-editsection-bracket">]</span></span></h2>
<h1><span class="mw-headline" id="Science_and_Technology">Science and Technology</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Teladi&amp;action=edit&amp;section=7" title="Edit section: Science and Technology">edit</a><span class="mw-editsection-bracket">]</span></span></h1>
<h3><span class="mw-headline" id="Ships">Ships</span><span class="mw-editsection"><span class="mw-editsection-bracket">[</span><a href="/index.php?title=Teladi&amp;action=edit&amp;section=8" title="Edit section: Ships">edit</a><span class="mw-editsection-bracket">]</span></span></h3>
<p>The Teladi are far more interested in trade than in developing new technologies. For that reason, their ships and stations use the outdated technologies that they've traded with the other races for. Because of their preference for trading, their ships often feature large cargo bays for their class. The <a href="/index.php/Kestrel" title="Kestrel">Kestrel</a> is perhaps the only Teladi ship with any sort of extraordinary capability, with its high speed. Teladi capital ships make extensive use of <a href="/index.php/Gauss_Cannon" title="Gauss Cannon">Gauss Cannons</a>, which is their principal capital-to-capital weapon. Overall, Teladi ships are below average to poor performance-wise, notable for having low speeds and poor firepower, although they have rather good shielding. They are often more expensive than average (though not so much, and there are a few exceptions) for ships in their class as well. For these reasons, Teladi ships are not popular with players. They do have a niche application that does not make them wholly ineffective in combat, however: their larger-than-normal cargo bays make them excellent for <a href="/index.php/Missiles" title="Missiles">missile</a> barrages, and it is not uncommon for certain ships like the <a href="/index.php/Falcon" title="Falcon">Falcon Hauler</a> and the <a href="/index.php/Shrike" title="Shrike">Shrike</a> to be stuffed with huge numbers of missiles, such as the <a href="/index.php/Tornado_Missile" title="Tornado Missile">Tornado</a> and the <a href="/index.php/Typhoon_Missile" title="Typhoon Missile">Typhoon</a>, two of the most effective missiles to strengthen the Teladi arsenal.
</p>
