					<div id="mw-content-text" lang="en" dir="ltr" class="mw-content-ltr"><p>The M4 class ships are interceptors by virtue of their speed, but they are also commonly used as fast light fighters. Normally the wingman of a <a href="/index.php/M3" title="M3">M3</a> ship as these ships are generally faster than the stronger <a href="/index.php/M3" title="M3">M3</a> class but slower and stronger than the <a href="/index.php/M5" title="M5">M5</a> class ships. A separate <a href="/index.php/M4%2B" title="M4+">M4+</a> class exists of ships with similar costs and armaments but better equipped for dogfighting.
</p><p>The Particle Accelerator Cannon that can be equipped on most M4 class ships makes them deadly against other fighters, as they can use their speed and maneuverability to their advantage whilst still dealing a respectable amount of damage to their opponents.
</p>
